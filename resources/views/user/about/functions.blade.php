                <p>
				La Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público-Privadas tendrá las siguientes funciones según el Decreto Municipal 883 de 2015 - Articulo 354:

							</p>

					<ul >
                       <li>Realizar la gestión requerida para garantizar la cualificación continua del paisaje, del patrimonio, de todos los equipamientos públicos y demás bienes inmuebles fiscales que hacen parte del Sistema Público y Colectivo, con el fin de incrementar los indicadores de calidad espacial y habitabilidad, y contribuir a la consolidación desde el aspecto físico espacial, de la plataforma requerida para que se implemente óptimamente el marketing de ciudad. </li>
                        <br>

					   <li>Liderar y/o formular, planificar y gestionar los proyectos enfocados a la cualificación continua del paisaje, del patrimonio, de todos los equipamientos públicos y demás bienes inmuebles fiscales, que hacen parte del Sistema Público y Colectivo.</li>
                       <br>

                    <li>Gestionar los proyectos ambientales, paisajísticos, urbanísticos, arquitectónicos y estéticos de conformidad con los Manuales de Diseño y Construcción del Espacio Público, Silvicultura, Protocolos de Construcción Sostenible y Ambientales urbanísticos - PAU-y la norma de sismo resistencia. </li>
                     <br>
                   
                       <li>Realizar la gestión para procurar los recursos requeridos para la cualificación del Sistema Público y Colectivo. </li>
                      <br>

					  <li>Realizar la gestión para procurar los recursos requeridos para la sostenibilidad y el mantenimiento, de todos los bienes inmuebles públicos que hacen parte del Sistema Público y Colectivo. </li>
                      <br>

					  <li> Realizar la gestión para garantizar que la sostenibilidad y el mantenimiento del Sistema Público y Colectivo, se realice de forma planificada y sistémica. </li>
                       <br>

					   <li>Realizar los análisis y estudios de reconocimiento de bienes inmuebles que conforman el sistema público y colectivo, como potenciales generadores de ingresos. </li>
					    <br>

						<li>Gestionar la transferencia de derechos de construcción, derechos fiduciarios para VIS y VIP, entre otros instrumentos de financiación del POT, destinados para la cualificación, sostenibilidad y mantenimiento de los bienes. </li>
					    <br>

						<li>Realizar la gestión requerida para la concreción de negocios inmobiliarios y demás que posibiliten generar ingresos, ya sea mediante asociaciones público-privadas y/o convenios públicos. </li>
					    <br>

						<li> Fomentar la inversión de capital privado en los proyectos de ciudad que busquen un mejoramiento de la calidad de vida de todos los habitantes.</li>
					    <br>


						<li>Implementar la política pública de inversión en proyectos de infraestructura física, transporte y movilidad, edificios públicos, temas de vivienda y ambientales, agua potable, residuos sólidos, energía, telecomunicaciones, sociales, educación y salud en los cuales se incluya capital privado. </li>
					     <br>


						 <li>Definir los parámetros de inversión en cada proyecto con un trabajo conjunto y coordinado en que puede participar el capital privado guardando siempre relación directa con el beneficio social entregado. </li>
                        <br>


						<li>Liderar la política de inversión y asociación de capital público y privado para la generación de desarrollo e inclusión, generando equidad entre los ciudadanos. </li>
                        <br>

					  <li> Generar al sector público un beneficio social y económico y posibilitar que el sector privado con su inversión obtenga una rentabilidad razonable con el capital a invertir. </li>	
                       <br>

					   <li>Estructurar, contratar y ejecutar directa, indirectamente o a través de los mismos capitales privados, los proyectos estratégicos que conjuntamente se determinen con las personas intervinientes para su administración, operación y control eficiente.</li>

                       <br>

					   <li>Emitir conceptos y absolver consultas acerca de la viabilidad en la formulación de proyectos sociales provenientes de iniciativas privadas en los cuales se invite a participar recursos públicos.</li>
					   <br>

                       <li>Coordinar las estrategias de promoción de la ciudad de Medellín dentro y fuera del país para hacerla atractiva a la inversión extranjera. </li>
                        <br>

						<li>Articular las iniciativas de alianzas públicas privadas del nivel central y los entes descentralizados.</li>
                        <br>

						<li>Las demás funciones asignadas en sus estatutos internos.</li>



					</ul>

		




