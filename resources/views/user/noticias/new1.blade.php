<h4 text-align="center">
Comerciantes de Medellín tienen descuentos hasta del 70% por el buen uso del espacio público 
</h4>

								<hr>


								<ul>
                                    
                                <li>La Alcaldía de Medellín prorrogó el Decreto 0967 de 2020 que impulsa la reactivación económica de la ciudad. 
</li>

<li>La medida favorece a comercios, bares y restaurantes con una reducción de aportes de hasta el 70 % por uso y aprovechamiento económico del espacio público.</li>

<li>Los establecimientos que apoyen al sector cultural con la contratación de músicos, artistas, cuenteros, entre otros, tendrán hasta un 15 % adicional de descuento.   </li>


</ul>
                                <hr>

								<p>Con el propósito de impulsar la reactivación económica del sector del entretenimiento, generar empleo, entregar un alivio financiero a los empresarios y favorecer la adecuación de espacios con protocolos de bioseguridad que garanticen la salud pública, la Alcaldía de Medellín prorrogó el Decreto 0967 de 2020 que establece una reducción temporal en la retribución económica que hacen los establecimientos por ocupación y uso del espacio público, así como por la utilización temporal de vías, franjas de espacio público, parques, plazoletas, entre otros, para actividades afines a los servicios comerciales.</p>
							
<p>La disminución temporal, que regirá entre julio de 2021 y febrero de 2022, comprende una baja del 70 % en el primer trimestre, 50 % en el segundo y 30 % en el tercer y último trimestre. Asimismo, los comercios que contraten artistas o profesionales del sector cultural tendrán hasta un 15 % adicional de disminución de aportes. </p>

<p>“Desde la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP– apoyamos la reactivación económica de la ciudad de manera decidida. Estos ahorros significan más empleo, mejores adecuaciones de los establecimientos y una invitación a cumplir la norma para cuidar juntos lo que es de todos”, explicó el director general de la Agencia APP, Rodrigo Foronda Morales.</p>

<p>Este apoyo económico es posible gracias a la articulación entre la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–, las secretarías de Hacienda y Cultura, y la Subsecretaría de Espacio Público.</p>

<p>“Esta iniciativa nos ha ayudado a ir recuperando todo lo que hemos perdido durante la pandemia por COVID-19. Poder tener más mesas y estar al aire libre es un atractivo para los clientes”, afirma Juanita Cobollo, propietaria de La Tasca Española y Cubata Bar. </p>

<p>Gracias al Decreto 0967 de 2020, que regirá hasta el 30 de junio de 2021, se han otorgado beneficios por más de $200 millones a 75 establecimientos de comercio. Asimismo se acompañó la reapertura segura de corredores gastronómicos como Astorga, Manila y Provenza, en El Poblado, con impacto en 74 establecimientos y la generación de 5.590 metros cuadrados de aprovechamiento del espacio público. Además, se ha incentivado el desarrollo de diferentes actividades en el espacio público, especialmente eventos ocasionales como bazares o ferias comerciales, favoreciendo a 372 personas.</p>

<p>Para Marcela Isaza, propietaria de Lleritas Bar, “este Decreto nos ha favorecido muchísimo. Hemos mejorado el aforo y el lugar porque a la gente le encanta estar al aire libre y sentarse a disfrutar sin aglomeraciones. Sin uso del espacio público podría adecuar únicamente cuatro mesas, en cambio, gracias al Municipio hoy puedo habilitar ocho mesas más”.  </p>

<p>Con la implementación de este incentivo económico se impulsa la activación de corredores y espacios estratégicos de Medellín Futuro, el acompañamiento a la pequeña y mediana empresa, la prestación de los servicios comerciales con distanciamiento social y la ampliación y optimización de espacios disponibles y seguros para todos.</p>


