<h4 text-align="center">
En la reactivación económica, la Alcaldía de Medellín acompaña a comerciantes para un mejor aprovechamiento 
del espacio público

</h4>

								<hr>


								<ul>
                                    
                                <li>Este año, la Agencia APP ha realizado más de 145 visitas técnicas a comerciantes y 20 encuentros de sensibilización.
</li>

<li> Desde la creación del instrumento de financiación, que busca el mejoramiento del paisaje de Medellín, se han recibido más de 7.000 solicitudes.     </li>

<li> Se espera que con la operación de este mecanismo creado por el POT, se cierre el cuatrienio con 4.500 personas jurídicas y naturales autorizadas para ocupar el espacio público.   </li>




</ul>
                                <hr>

								<p>
                                    

                                Este año, y como parte del acompañamiento a la reactivación económica de la ciudad, la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas - APP ha realizado más de 145 visitas técnicas y 20 charlas de sensibilización con la participación de 200 comerciantes en torno al aprovechamiento adecuado del espacio público.
							
<p>


  Entre los participantes están Aso Food Trucks, Mercados del Río, Asobastilla, Provenza, Asoguayaquil y la carrera 33, en El Poblado, especialmente ahora que los protocolos de bioseguridad recomiendan más de dos metros entre mesas y espacios abiertos con mayor circulación de aire en los establecimientos.
<p>
  El director general de APP, Rodrigo Foronda Morales, explicó: “Miles de  comerciantes de la ciudad se acercaron a la Administración Municipal para que juntos trabajáramos en el florecimiento y la prosperidad de esos negocios de los particulares, que están aprovechando el espacio público y están reconociendo ese esfuerzo que  se está haciendo para que ellos puedan acceder a ese espacio público y puedan asociarse para que crezcan sus negocios”.   
<p>
  Desde la creación del instrumento de financiación que busca el mejoramiento del paisaje de Medellín, se han radicado más de 7.000 solicitudes ante la Administración Municipal para hacer uso del espacio público, entre transitorias y temporales, con una retribución para la ciudad de más de $2.000 millones que se invierten en el mejoramiento del sistema público colectivo.  
<p>
  “Vimos el gran potencial que tenía el espacio público, hicimos un aprovechamiento y embellecimiento del área y cambiamos la cara de un área que antes era oscura y que daba la sensación de inseguridad”, afirmó María Paulina Suescún, socia del restaurante Repeat, un establecimiento ubicado en el sector Provenza de El Poblado, creado bajo el concepto de outdoor dining con café, restaurante y bar. 


<p>
  Otro ejemplo es Social Tienda Mixta, también en Provenza, negocio que nació entre amigos hace doce años. Su socio y gestor, Samuel Restrepo, expresó que el espacio público ha sido un asunto estratégico desde el origen del restaurante-bar. 
<p>
“A veces las administraciones no tienen todas las herramientas, pero con la Agencia APP hemos tenido toda la disposición para legalizar el espacio público y ponerlo al servicio de las personas para disfrutar del clima y del paisaje exterior”, dijo.
<p>
<p>
Para el cierre del cuatrienio, se espera tener 2.500 personas jurídicas y naturales que aprovechen económicamente el espacio público y 4.500 autorizadas. 
</p>

<p>
Quienes deseen legalizar este uso deben radicar la documentación en el Centro Administrativo Municipal; posteriormente, la Agencia APP evaluará aspectos normativos, paisajísticos, urbanos, patrimoniales, entre otros, y determinará su viabilidad de acuerdo con el propósito de hacer de Medellín una ciudad más amigable y equitativa para todos. 
</p>

<p>
En 2014 fue creado por el POT el instrumento de financiación Aprovechamiento Económico del Espacio Público –AEEP– para motivar su apropiación y consolidar una fuente alternativa de recursos no tributarios que faciliten su cualificación, sostenibilidad y mantenimiento. Esto ha contribuido a motivar las buenas prácticas, reducir la espontaneidad en el uso del espacio público, disminuir riesgos y estimular el autocontrol comercial. En 2014 fue creado por el POT el instrumento de financiación Aprovechamiento Económico del Espacio Público –AEEP– para motivar su apropiación y consolidar una fuente alternativa de recursos no tributarios que faciliten su cualificación, sostenibilidad y mantenimiento. Esto ha contribuido a motivar las buenas prácticas, reducir la espontaneidad en el uso del espacio público, disminuir riesgos y estimular el autocontrol comercial. 
