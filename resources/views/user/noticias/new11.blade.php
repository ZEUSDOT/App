<h4 text-align="center">
Inician estudios para la construcción del Hotel Plaza Mayor 

</h4>

								<hr>


								<ul>
                                    
                                <li>El hotel estaría ubicado en la Zona Dura, al lado del Pabellón Amarillo hacia la Avenida 33, o en la zona de Los Búcaros, junto a Caja de Madera, de acuerdo con el resultado de los estudios previos.
</li>

<li> La iniciativa tiene como objetivo fortalecer y ampliar la capacidad logística de Medellín y Plaza Mayor en lo que a turismo se refiere.   </li>






</ul>
                                <hr>

								<p>
                                    

                                Este miércoles, se firmó el contrato para iniciar los estudios de prefactibilidad del potencial hotel de Plaza Mayor, un proyecto histórico que movilizará la economía de Medellín.
							
<p>


  
Inicialmente, se adelantará un informe en el que se integrarán aspectos técnicos, jurídicos y financieros que permitirán tener un diagnóstico base, un posterior diseño conceptual y una modelación financiera y económica preliminar que permitan determinar su viabilidad.

<p>
En la firma del documento estuvieron el secretario de Desarrollo Económico, Alejandro Arias García; el secretario de Hacienda y presidente de la Junta Directiva de Plaza Mayor, Óscar Hurtado Pérez; el director de la Agencia APP, Rodrigo Foronda y el gerente general de Plaza Mayor, Víctor Hugo Zapata.
<p>
“Por fin firmamos el contrato de los estudios de prefactibilidad, los cuales nos dirán si es posible sacar adelante este proyecto. Tenemos confianza y ahora más que nunca trabajamos fuerte por la reactivación económica del sector para la proyección internacional de nuestra ciudad-región”, dijo el gerente general de Plaza Mayor, Víctor Hugo Zapata Madrigal.
<p>
  
El hotel de Plaza Mayor sería para Medellín y Antioquia un potencializador del turismo de negocios y reuniones, un factor que dinamizaría la economía y una ventana adicional para el fortalecimiento competitivo de la ciudad en la captación de eventos internacionales. 



<p>
El secretario de Desarrollo Económico, Alejandro Arias García, expresó que “el turismo recreativo y de negocios, que representa el 8 % del PIB de Medellín, debe ser uno de los pilares de nuestra reactivación económica, por su capacidad de generar bienestar y empleos. La apuesta del Hotel Plaza Mayor es clave para hacernos más atractivos para el mercado internacional gracias al trabajo conjunto del sector público, privado y todos los actores de la sociedad”. 

<p>
Con recursos propios de Plaza Mayor por $487 millones, la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP iniciará el estudio de prefactibilidad el cual irá hasta diciembre de 2021 y, posteriormente, si el resultado es positivo, se realizaría el de factibilidad.
<p>
<p>
“El Hotel Plaza Mayor es un sueño que generará importantes ingresos y convertirá a la ciudad en un referente internacional de grandes eventos. Los estudios que realizaremos desde la Agencia APP constan del marco normativo, es decir, todo el aspecto jurídico tanto predial como del Plan de Ordenamiento Territorial, la posibilidad de hacer la alianza público privada, la proyección arquitectónica y los estudios técnicos, topográficos y de ingeniería”, agregó el director general de la Agencia APP, Rodrigo Hernán Foronda Morales.

</p>

<p>
Plaza Mayor está en permanente estructuración de proyectos para sorprender a sus visitantes, turistas y ciudadanos. Además, articulada con el Plan de Desarrollo Medellín Futuro 2020-2023, busca el fortalecimiento de la ciudad como un epicentro de negocios y encuentros, ejemplo de innovación, optimismo y sostenibilidad, con proyectos significativos como este que generan mensajes de esperanza y motivación tanto para el sector turístico, como para los subsectores que hacen parte de esta cadena. 

</p>




