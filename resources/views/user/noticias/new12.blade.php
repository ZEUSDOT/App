<h4 text-align="center">
Intervención artística se toma los bajos de Plaza de la Libertad como un aporte a la renovación del paisaje urbano de Medellín

</h4>

								<hr>


								<ul>
                                    
                                <li> Será la primera de una serie de obras que estarán en diferentes puntos de la ciudad. 
</li>

<li>  Los artistas elegidos por convocatoria pública para este trabajo fueron Angy Melissa García y Bryan Sánchez.  </li>






</ul>
                                <hr>

								<p>
                                    

                                Un jaguar y la representación gráfica de una ciudad orgullosa de su naturaleza y sus símbolos arquitectónicos serán protagonistas de la fachada de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas (Agencia APP). Se trata de una iniciativa artística que contribuye a renovar el paisaje del Centro y la primera de una serie de obras de arte urbano que estarán en diferentes lugares de Medellín. 
							
<p>


  
Tras una convocatoria pública en la que participaron diferentes creadores, los ganadores fueron la diseñadora gráfica y grafitera, Angy Melissa García, y el artista plástico y muralista, Bryan Sánchez.

<p>
La obra de Angy Melissa es un homenaje a una Medellín que mira hacia el futuro a través del color, sin dejar de lado sus tradiciones. Ella vive en la comuna 3 (Manrique) y desde muy joven se interesó por el grafiti. Entre 2007 y 2008 mejoró su técnica y se dedicó a pintar los muros con las características del wildstyle, que entrelaza letras con formas complejas y retadoras. 

<p>
Bryan Sánchez es un muralista y tatuador que aborda temas como la fauna vulnerable y la importancia de su cuidado y preservación. Define su estilo como sketchy watercolor, una apuesta por las imágenes inconcretas, como un bosquejo en el papel. “Me gusta dejar zonas sin terminar y con errores premeditados como los colores sólidos fuera de línea”, explica. Es el primer latinoamericano con su propio set de tintas para la marca internacional Eternal Ink.

<p>
  
El jurado estuvo conformado por Juan David Quintero (curador de arte), Natalia Martínez (relacionista pública), Carlos Jacanamijoy (artista) y el director general de la Agencia APP, Rodrigo Foronda Morales.




<p>
“El arte urbano es otro instrumento que tiene Medellín para acompañar e impulsar la reactivación laboral de los artistas, transformar los espacios con luz y color, tocar el alma de los ciudadanos y cualificar el espacio público para su aprovechamiento y disfrute”, dijo Foronda Morales.

<p>
Después de esta intervención se continuará con la galería de arte en Prado, en la calle Barranquilla, para promover el turismo y la recuperación de este barrio patrimonial. 

<p>
<p>
La iniciativa es liderada por la Agencia APP, que desde 2012 trabaja por el bienestar integral de las condiciones de vida de los habitantes, a través de la gestión, promoción y acciones que garanticen la cualificación, sostenibilidad y mantenimiento del Sistema Público y Colectivo, por generar recursos a través de la implementación de los instrumentos financieros definidos en el POT y por la incorporación de capitales privados a proyectos de inversión. 


</p>




