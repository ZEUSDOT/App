								<p>
                                    

                                Patrimonio, cultura, vecindad, barrio, homenaje a grandes personajes locales y otras expresiones artísticas podrían ser parte de la futura cara urbana que tendrá la calle Barranquilla, gracias a la invitación que realiza la Agencia APP –Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas– para la renovación de fachadas de este corredor vial del norte de Medellín.
							
<p>


  
Muralistas, grafiteros, artistas plásticos o visuales, y otros creativos de la ciudad y del área metropolitana podrán inscribir sus portafolios hasta el próximo martes 18 de agosto. La publicación de los escogidos será el 24 de agosto.

<p>
Para ello, el Comité de Selección tendrá en cuenta aspectos como la pertinencia de los ejes investigativos del artista o del colectivo en el contexto del barrio Prado; la trayectoria, coherencia y constancia de los participantes; la capacidad o experiencia en la realización de trabajos investigativos, como residencias artísticas, actividades de campo, etnografías o prácticas artísticas en entornos comunitarios; y el desarrollo formal, la solidez técnica y la materialización de proyectos similares.

<p>
Esta iniciativa, liderada por la Agencia APP, hace parte del plan Prado Vive su Patrimonio, que tiene como objetivo conservar el patrimonio arquitectónico, histórico y cultural de este tradicional sector de la ciudad.

<p>
  
De acuerdo con Rodrigo Foronda, director general de la Agencia APP, “de esta manera contribuimos también con la reactivación económica de la ciudad, especialmente del sector artístico. Es muy importante, para un sector como un Prado,
llenarlo de arte, de color y de magia con el acompañamiento de la Administración Municipal”.





<p>
Quienes deseen participar pueden ingresar a <a href="www.app.gov.co">www.app.gov.co</a>, inscribirse y seguir las instrucciones. No se pierda esta oportunidad de ser protagonista de la transformación urbana de Prado y de Medellín.

<p>

<h3 style="text-align:center;">
Sobre la Agencia APP
</h3>

<p>
La Agencia APP es un ente descentralizado de la Alcaldía de Medellín, creado en
2012 para procurar el bienestar integral de las condiciones de vida de los habitantes, a través de la gestión, promoción y acciones que garanticen la cualificación, sostenibilidad y mantenimiento del Sistema Público y Colectivo. Además, es responsable de promover la generación de recursos a través de la implementación de los instrumentos financieros definidos en el POT y de fomentar la incorporación de capitales privados a proyectos de inversión para beneficio de todos.

</p>




