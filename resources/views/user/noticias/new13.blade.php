								<hr>


								<ul>
                                    
                                <li>Muralistas, grafiteros, artistas plásticos o visuales, y otros creativos podrán inscribir sus portafolios hasta el martes 18 de agosto.
</li>

<li>  Esta es una oportunidad para entregarle a la ciudad una nueva galería de arte que promueva el turismo y la recuperación del barrio Prado. </li>

<li>
Quienes deseen participar pueden ingresar a <a href="www.app.gov.co">www.app.gov.co</a>, inscribirse y seguir las instrucciones.
