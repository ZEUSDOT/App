<h4 text-align="center">
Medellín fue nominada a premios latinoamericanos por gestión innovadora y sostenible del barrio Prado


</h4>

								<hr>


								<ul>
                                    
                                <li>El Plan Integral de Gestión de Prado, liderado por la Agencia APP, es uno de los proyectos finalistas por Colombia en los Latam Smart City Awards. 
</li>

<li>  Los ganadores se conocerán el 6 de octubre en el marco del Smart City Expo Latam que se realizará en Mérida, Yucatán (México). </li>







</ul>
                                <hr>

								<p>
                                    

								Gracias a las acciones integrales en el territorio que ha realizado la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–, el Plan Integral de Gestión de Prado ha sido nominado en la categoría “Desarrollo Urbano Sostenible y Movilidad” en los premios Latam Smart City Awards.
							
<p>


  
Estos premios reconocen los planes, políticas y soluciones tecnológicas que mejoran la calidad de vida en las ciudades, así como las acciones disruptivas y favorables que se desarrollan en América Latina en favor de los ciudadanos. 

<p>
“En Medellín estamos felices. La Agencia APP fue seleccionada como finalista en los Latam Smart City Awards que premiará los proyectos más innovadores y sostenibles de América Latina. Felicitaciones a su director, Rodrigo Foronda, y a todo el equipo por esta nominación que mejora las zonas comunes y las fachadas del barrio Prado, preservando su historia de más de cien años y aportando a la construcción de la Ecociudad. Estamos seguros que traeremos este premio para Medellín”, afirmó la secretaria Privada de la Alcaldía de Medellín, María Camila Villamizar Assaf. 

<p>
Junto a esta iniciativa también se encuentran nominados, en la misma categoría, la Actualización del Plan Urbano Ambiental del Gobierno de Buenos Aires y la Recuperación de la Ribera del Río Cupatitzio de México.

<p>
El director general de la Agencia APP, Rodrigo Foronda Morales, manifestó: “para nosotros es un orgullo estar nominados a estos premios. Nuestra entidad se ha volcado en el único barrio patrimonial de Medellín a realizar una reconversión del territorio con arte urbano, la puesta en valor del patrimonio, iluminación arquitectónica de fachadas y la reestructuración del Parque Olano y la calle Jorge Robledo. Esta clasificación reconoce y destaca estos esfuerzos que ha realizado el Municipio de Medellín a través de nuestra entidad”. 





<p>
El barrio Prado fue fundado en 1920 por la élite empresarial local. Tras casi 100 años de sufrir los embates del tiempo, la Agencia APP planea y ejecuta el Plan Integral de Gestión de Prado, bajo el lema Prado Vive, para atender sus problemáticas a través de la puesta en valor del patrimonio cultural con intervenciones urbanísticas sostenibles y con la promoción de nuevas actividades económicas creativas y culturales.
<p>

<p>
Entre las acciones más destacadas se encuentran el mejoramiento de 11.800 metros cuadrados de espacio público, la intervención de 3.350 metros cuadrados de murales y fachadas con la Galería de Arte Urbano a Cielo Abierto de la calle Barranquilla, la iluminación arquitectónica de 28 Bienes de Interés Cultural (BIC) y los 4.292 metros cuadrados de nuevo espacio público del Parque Prado.

</p>


<p>El Smart City Expo fue creado en 2011 por Fira Barcelona, una de las organizaciones feriales más importantes de Europa y la edición Latinoamericana, organizada por Fira Barcelona – México, se realizará en Mérida, Yucatán (México), entre el 5 y el 14 de octubre de 2021.
</p>

<p>
Bajo el lema “Repensar, Reactivar, Reconstruir” se espera que a este evento asistan aproximadamente 13.000 personas (12.000 virtuales y 1.000 presenciales) provenientes de 300 ciudades de 45 países. Además, se calcula que participarán más de 200 instituciones y empresas y más  de 300 conferencistas. Los ganadores serán dados a conocer  el próximo 6 de octubre. 

</p>



