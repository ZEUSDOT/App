<h4 text-align="center">

Con una intervención artística y cultural, Medellín impulsa la reactivación económica en el Parque Lleras y Provenza



</h4>

								<hr>


								<ul>
                                    
                                <li>El Plan de reactivación en la zona rosa de El Poblado ha permitido la creación de 226 nuevos empleos recientemente. 
</li>

<li>  Con exposición de arte urbano en las calles del Parque Lleras y Provenza, la Alcaldía de Medellín invita a la ciudadanía a recorrer estos espacios en el marco de la Feria de las Flores.  </li>

<li>
Desde la reactivación de las actividades comerciales en la Comuna 14 - El Poblado, se realizan intervenciones y acompañamiento institucional para velar por la tranquilidad de sus visitantes.
</li>





</ul>
                                <hr>

								<p>
                                    

								La Alcaldía de Medellín dispuso la ampliación del plan de reactivación del Parque Lleras y Provenza, sectores comerciales y de esparcimiento ubicados en el corazón de El Poblado y donde se concentra gran parte del flujo de turistas que visitan la ciudad, especialmente durante la Feria de las Flores.
							
<p>


  
“La idea es que la gente venga a disfrutar, no solo de las Feria de las Flores, sino Medellín todo el año. Disfrutar Provenza y el Parque Lleras, que es un ícono de la ciudad, un espacio maravilloso y en el que esta administración está metiendo toda la ficha para recuperarlo”, aseguró Daniel Quintero Calle, alcalde de Medellín. 

<p>
El trabajo articulado de la Alcaldía de Medellín con los comerciantes posibilitó la generación de 226 empleos directos. Resaltando que durante el último mes se ha presentado un crecimiento del 35% en el ingreso económico de los establecimientos.

<p>
Esta es una intervención cultural, para la transformación física y simbólica de los espacios, promoviendo la reactivación económica y mejorar la percepción de seguridad. En este sentido, los comerciantes del sector han manifestado tener contemplado generar nuevos empleos en el presente mes como respuesta a las nuevas necesidades y condiciones económicas del sector.

<p>
“Nos hemos encontrado con un escenario muy interesante, muy esperanzador para todos los comerciantes de la zona. La Alcaldía ha venido trabajando en un proyecto de reactivación donde nos hemos visto ampliamente beneficiados, así como nuestros usuarios. Hemos activado zonas exteriores que han sido fundamentales para que la gente se sienta segura para seguir visitando la zona, a seguir compartiendo en un escenario mucho más bioseguro. Estamos muy contentos con todo este proceso”, agregó Sebastián Garcés, comerciante de Provenza.





<p>
Con el propósito de fortalecer la seguridad en la zona se han desarrollado acciones institucionales como la instalación del Puesto de Mando Unificado (PMU) que funciona exclusivamente para la Comuna 14, se extenderá de indefinidamente para coordinar de manera articulada, las acciones operativas de la Policía Metropolitana, el monitoreo de las zonas de esparcimiento y las labores de los funcionarios de las dependencias de la Alcaldía de Medellín, para garantizar el orden, la seguridad y el cumplimiento de las normas de bioseguridad en los locales abiertos al público. 
<p>

<p>
Un total de 1.620 metros cuadrados fueron intervenidos con obras de arte urbano temporal gracias a la colaboración de los artistas Deúniti, 6B Grafito, Apolo, Trash Art, Ácido y Las chicas que pintan y colectivos de la ciudad que con su arte le dieron color y vida a las calles de este sector de El Poblado.

</p>


<p>Asimismo, fueron dispuestos 500 metros cuadrados para el aprovechamiento económico del espacio público, con el objetivo de impulsar el crecimiento de los comerciantes, favorecer la ampliación de espacios bioseguros y brindarle a la ciudadanía y turistas espacios más cómodos, acogedores y seguros alrededor del ocio y del entretenimiento. Esta estrategia también se realizó en los sectores de Manila y Astorga.  
</p>

<p>
Para fortalecer el restablecimiento social en el Parque Lleras y Provenza, se han realizado jornadas de sensibilización con equipos especializados sobre la explotación sexual y comercial de niños, niñas y adolescentes, trabajo infantil y población en situación de calle. Esta atención se complementa con la disposición de toda la oferta institucional, para proporcionar herramientas a las personas vinculadas a estas problemáticas. 


</p>

<p>
Se han realizado además jornadas de aseo y ornato con acciones de poda, tala, mantenimiento a zonas verdes, lavado de zonas duras, pintura de pasamanos, canecas, postes y murales, con la presencia permanente de Emvarias en el sector.
</p>

<p>
Con este acompañamiento institucional en la zona rosa de Medellín, la Administración Municipal busca no solo propiciar puntos de encuentro seguros a partir de la reactivación económica del Parque Lleras y Provenza, sino también cualificar el paisaje con arte, de la mano de importantes colectivos y artistas urbanos de la ciudad. 
</p>



