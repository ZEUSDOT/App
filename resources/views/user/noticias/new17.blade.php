<h4 text-align="center">

Medellín celebra el Mes del Patrimonio con recorridos, maratones fotográficas y charlas 


</h4>

								<hr>


								<ul>
                                    
                                <li> Son más de 50 actividades que invitan a los ciudadanos a conocer y valorar la diversidad patrimonial de Medellín.
</li>

<li>  Charlas, exposiciones, maratones fotográficas y recorridos por el Centro y por los árboles patrimoniales de los barrios hacen parte de la programación.  </li>

<li>
Participan más de 40 organizaciones vinculadas con la Espiral de las Memorias y los Patrimonios Culturales de Medellín.
</li>



</ul>
                                <hr>

								<p>
                                    

								Con el lema “El patrimonio te lleva a otra dimensión”, la Alcaldía de Medellín invita a celebrar en septiembre el Mes del Patrimonio para que los ciudadanos reconozcan, divulguen, valoren y se apropien de los bienes materiales e inmateriales que son patrimonio de todos.
							
<p>


  
Desde hoy serán más de 50 actividades realizadas por la Secretaría de Cultura Ciudadana, la Gerencia del Centro, la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–, y la Secretaría de Medio Ambiente.

<p>
Por medio de charlas, talleres, mercados artesanales y recorridos presenciales con aforos controlados y protocolos de bioseguridad, se busca acercar a los medellinenses a los bienes patrimoniales naturales, arquitectónicos, artísticos, documentales y culturales de la ciudad. 

<p>
Desde el programa de Memorias y Patrimonios, de la Secretaría de Cultura Ciudadana, se construyó una agenda colaborativa con más de 40 organizaciones que integran la Espiral de las Memorias y los Patrimonios Culturales de Medellín.

<p>
“Este mes, septiembre, como cada año, lo dedicamos al patrimonio, no solamente para entender lo que fuimos, sino también para hacer reflexiones sobre lo que podemos ser como una ciudad educada. Este año, en el portafolio de estímulos de la Secretaría de Cultura, abrimos nueve de ellos dedicados a los diferentes patrimonios inmateriales y materiales de nuestra ciudad. Durante este mes conoceremos algunos de los avances de estas investigaciones creativas”, destacó el subsecretario de Bibliotecas, Lectura y Patrimonio, Sebastián Trujillo Osorio.


<p>
En el Centro de Medellín, esta celebración toma una connotación muy especial debido a que más del 80 % del patrimonio declarado con el que cuenta la ciudad está ubicado en la comuna 10 – La Candelaria.  Por eso, desde la Gerencia del Centro se propone un viaje hacia el patrimonio, a través de caminatas y maratones fotográficas, además de charlas y conversatorios sobre su riqueza patrimonial y el patrimonio intangible. 
</p>

<p>
“Inicia uno de los meses más emocionantes para la Gerencia del Centro. Tenemos charlas, recorridos, conversatorios, activaciones del espacio público y una agenda muy especial para nuestro Distrito Patrimonial en el barrio Prado. Acompáñenos a disfrutar el arte, la cultura y el patrimonio en Medellín”, indicó la gerente del Centro, Mónica Pabón.
</p>

<p>
La Agencia APP ofrecerá actividades sobre la memoria y la historia de la ciudad. En la Casa Ángel, considerada una de las más grandes y suntuosas del barrio Prado, se realizará un bazar de emprendimientos del sector; la muestra fotográfica Mi Casa, Mi Museo, sobre objetos como “patrimonios íntimos”; la exposición Medellín de calles y gentes del fotógrafo Juan Fernando Ospina y charlas académicas.
</p>

<p>
Asimismo, se harán recorridos guiados, con inscripción previa, por la carrera Palacé, el Parque Bolívar, la Avenida Jardín y la calle 8, en El Poblado, para identificar árboles patrimoniales y otras intervenciones de paisajismo, preservación del patrimonio y mejoramiento del espacio público que ha realizado la Agencia APP. 
</p>

<p>
“Invitamos a toda la ciudadanía de Medellín a visitar Prado, a visitar nuestro barrio patrimonial, a llenarse de historia, a llenarlo de vida, que es lo que nos corresponde, sobre todo, en el Mes del Patrimonio”, afirmó el director general de la Agencia APP, Rodrigo Foronda Morales.
</p>

<p>
La Secretaría de Medio Ambiente también se suma con visitas guiadas y charlas sobre la importancia del cuidado y conservación de los 697 árboles y palmas que son patrimonio natural y cultural de la ciudad. En barrios como Prado y Provenza (El Poblado) estos individuos arbóreos son un referente de identidad y memoria cultural, y tienen un valor histórico, simbólico y ecológico.
</p>

<p>
Desde 1998, el Ministerio de Cultura promueve la celebración del Mes del Patrimonio en Colombia, con el fin de abrir un espacio para reflexionar sobre la importancia de reconocer, salvaguardar, conservar, proteger y difundir los valores culturales y patrimoniales de todas las regiones del país. Medellín se suma este año con la articulación entre la institucionalidad y las iniciativas ciudadanas para que así el patrimonio no sea solo lo heredado, sino también lo construido entre todas y todos.
</p>

