<h4 text-align="center">

Medellín clausura el Mes del Patrimonio desde el barrio Prado que conserva el 70 % de los Bienes de Interés Cultural de la ciudad


</h4>

								<hr>


								<ul>
                                    
                                <li> El #MesdelPatrimonio2021 termina con charlas, conferencias, muestra de artesanías, exposiciones y otros eventos que estarán abiertos a la ciudadanía. 
</li>

<li>  La cita será del viernes 24 al miércoles 29 de septiembre en Casa Ángel, uno de los Bienes de Interés Cultural del único barrio patrimonial de Medellín.  </li>

<li> Prado es el centro de la programación del Mes del Patrimonio porque conserva más de la mitad de los 1.082 Bienes de Interés Cultural de la ciudad.
 </li>



</ul>
                                <hr>

								<p>
                                    

								Como parte de la conmemoración del Mes del Patrimonio, la Casa Ángel, uno de los lugares emblemáticos del barrio Prado, se convertirá entre el 24 y el 29 de septiembre en un espacio para el reconocimiento del patrimonio de la ciudad. 
							
<p>


  
Exposiciones, charlas, talleres, entre otras actividades, harán parte de un gran festival que se vivirá alrededor de la cultura, bajo el liderazgo de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–, con el propósito de sensibilizar a la ciudadanía sobre los patrimonios arquitectónico, cultural, ambiental, gastronómico y social.  


<p>
“Invitamos a la ciudadanía a que nos acompañe en el barrio Prado para que conozcan las diferentes acciones y estrategias que estamos desarrollando para la conservación, preservación y puesta en valor del patrimonio de la ciudad. Es una invitación también para conocer el barrio, caminarlo, ver el embellecimiento de las fachadas, conocer los diferentes Bienes de Interés Cultural y el nuevo Parque Prado”, afirmó el subdirector de Gestión de Paisaje y Patrimonio de la Agencia APP, Daniel Madrigal Arango. 

<p>
La Casa Ángel es un espacio con techos altos, salones amplios y vitrales medievales que imitan las ventanas de catedrales góticas. Es un Bien de Interés Cultural porque en ella habitó la familia Ángel, una de las más prestigiosas del siglo XX en Antioquia. Además, por su ubicación, dimensión y por tener características de la arquitectura tradicional y moderna. 


<p>
Esta casona estará abierta entre las 10:00 a. m. y las 5:00 p. m. Allí la ciudadanía podrá ver las exposiciones fotográficas, Medellín de calles y gentes, del fotógrafo Juan Fernando Ospina; retratos del archivo histórico de Laureles en cooperación con Un Punto Fijo Café; Mi Casa, Mi Museo, una muestra que hace homenaje a los objetos que han acompañado a residentes del barrio durante parte de sus vidas, como un ejercicio de memoria personal; y Prado Vive, una exhibición con las iniciativas que adelanta la Agencia APP para la recuperación y fortalecimiento integral del barrio Prado.


<p>
El sábado 25 de septiembre se realizarán jornadas académicas a las 10:00 a. m. y a las 11:30 a. m., y a las 2:00 p. m. y 3:00 p. m. con ponencias alrededor del patrimonio y la música antioqueña.  Asimismo, durante el fin de semana, el 25 y 26 de septiembre, habrá muestra y venta de artesanías de emprendedores del territorio y una iniciativa llamada “El Poncherazo”, que permitirá a los asistentes tomarse y llevarse una fotografía. 
</p>

<p>
El viernes y el sábado, a las 8:00 p. m. y 9:00 p. m., se realizará una proyección de video mapping en la fachada de esta casona y se podrá apreciar la intervención de espacio público e iluminación arquitectónica, realizada por la Agencia APP sobre la carrera Palacé. Para consultar estos y otros eventos del mes del Patrimonio, los ciudadanos pueden ingresar a <a href="https://patrimoniomedellin.gov.co/" target="_blank">https://patrimoniomedellin.gov.co/</a> 

</p>

<p>
Esta es una invitación abierta y gratuita para que los ciudadanos visiten el único barrio patrimonial de Medellín, recorran el Parque Prado y se encuentren en esta casa para vivir y sentir la cultura local. 
</p>

<p>

El origen de Prado se remonta a 1926, cuando el industrial Ricardo Olano lideró su urbanización. El modelo de barrio jardín, dominante en Europa, sirvió de inspiración para este proyecto de gran acogida entre las élites económicas de la época, quienes querían diferenciarse a partir de un conjunto residencial que resaltara el prestigio y el respeto del sector y sus habitantes. 

</p>

