<h4 text-align="center">

La Alcaldía de Medellín invita a los artistas urbanos a presentar o renovar su portafolio de obras


</h4>

								<hr>


								<ul>
                                    
                                <li> Muralistas, grafiteros, artistas plásticos o visuales, y otros creativos pueden inscribir sus portafolios en <a href="https://www.app.gov.co/" target="_blank">www.app.gov.co</a>
</li>

<li>  Los portafolios inscritos serán tenidos en cuenta para futuras intervenciones de arte urbano, como aporte a la reactivación económica. </li>

<li> Medellín es la única ciudad del país que tiene un indicador de arte urbano en el actual Plan de Desarrollo. La meta es alcanzar 14.300 metros cuadrados de cualificación del paisaje.
 </li>



</ul>
                                <hr>

								<p>
                                    

								Llenar de arte, magia y color a la Medellín Futuro. Bajo esta premisa la Administración Municipal, a través de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–, abre la invitación para que artistas urbanos, grafiteros, muralistas, artistas plásticos y demás miembros del sector artístico presenten o actualicen su portafolio de obras con el propósito de consolidar un banco de proyectos que acompañe la cualificación del paisaje local.
							
<p>


  
Para la revisión y selección de los artistas, que serán llamados para las próximas intervenciones de arte urbano, se contará con el acompañamiento de Titania y Juan Vélez aka Pachamama, ambos reconocidos artistas del sector. La primera es diseñadora gráfica, ilustradora y artista urbana colombiana con más de once años de experiencia. Pachamama, por su parte, es artista visual, magíster en artes visuales de la Universidad Nacional Autónoma de México, docente y con amplia experiencia en procesos de investigación y gestión cultural. 


<p>
Para el subdirector de Gestión de Paisaje y Patrimonio de la Agencia APP, Daniel Madrigal Arango, “esta es una invitación con la cual pretendemos cualificar y mejorar el paisaje urbano, generar unas zonas de reactivación económica y aportar a diferentes expresiones artísticas y culturales de la ciudad”.  

<p>
Con intervenciones como la galería urbana a cielo abierto de la calle Barranquilla (en el nororiente), la fachada de la sede administrativa de la Agencia APP, el intercambio vial Bulerías, las obras en el parque Lleras y en Provenza, y en los puentes de la Avenida 33, la Avenida Guayabal, la calle Colombia y en el barrio Caribe, a la altura de la Secretaría de Movilidad, Medellín suma una ejecución de más de 7.000 metros cuadrados de arte urbano de los 14.300 metros establecidos en el Plan de Desarrollo Medellín Futuro 2020-2023.  


<p>
El pintor urbano, César Figueroa, ha participado en diferentes iniciativas de carácter gubernamental, entre ellas, en la calle Barranquilla. Para él, “este tipo de invitaciones nos permiten desarrollar ideas que vamos descubriendo, proyectarlas y volverlas un hecho en grandes infraestructuras de ciudad”. 


<p>
Esta invitación abierta y permanente es también una oportunidad para apoyar la reactivación económica del sector artístico, movilizar los procesos culturales en la ciudad, diversificar las manifestaciones de arte urbano con diferentes estilos y formas, y avanzar en la consolidación de Medellín como una galería de arte abierta para el disfrute de todos.  
</p>

<p>
Los artistas interesados en presentar por primera vez su portafolio o actualizarlo pueden hacerlo en <a href="https://www.app.gov.co/" target="_blank">www.app.gov.co</a>


</p>

