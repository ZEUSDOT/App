<h4 text-align="center">
Alcaldía de Medellín entregó  los muros verdes del Concejo Municipal y el Centro Administrativo la Alpujarra 
</h4>

								<hr>


								<ul>
                                    
                                <li>Son 475 m2 de jardín vertical en la fachada del Concejo de Medellín con 22.800 plantas sembradas.   
</li>

<li>A esta obra se suman 292 m2 de jardines verticales al interior del Centro Administrativo la Alpujarra. </li>

<li>Estas obras de infraestructura verde, las más grandes de la ciudad,  contribuyen al bienestar de funcionarios, contratistas y ciudadanos, y consolidan a Medellín como una Ecociudad.     </li>


</ul>
                                <hr>

								<p>
                                    
                                Con el propósito de cualificar el paisaje público urbano, mejorar la conectividad biológica y adecuar espacios para enriquecer y consolidar el sistema ambiental, la Alcaldía de Medellín entregó 767 m2 de nueva infraestructura verde alternativa: uno de los muros verdes más grandes de la ciudad de 475 m2 y 292 m2 de jardines verticales en las zonas de recepción, vacío central y piso quinto del Centro Administrativo La Alpujarra. 

                                </p>
							
<p>
En total se sembraron 36.816 plantas que incluyen especies como liriope variegado y sencillo, incienso, vinca, bergenia y helecho peine, entre otras, que aportan a la biodiversidad de fauna y flora en la ciudad. En ambas obras se invirtieron 948 millones de pesos y se generaron 59 empleos entre profesionales, contratistas y mano de obra no calificada.  </p>

<p>
“Estos son proyectos muy bonitos que embellecen la ciudad y que tienen un poder funcional ambiental bárbaro. En este caso tenemos cinco muros verdes en el CAM, pero también cuatro puentes peatonales que ya estamos interviniendo. En total vienen muchos muros verdes y la intervención en general va a sumar un millón de metros cuadrados”, expresó el alcalde Daniel Quintero Calle.    </p>

<p>
Las intervenciones reducen la temperatura al interior de las edificaciones, regulan la temperatura ambiente y del entorno, contribuyen a la purificación del aire, captan contaminantes y material particulado presentes en el aire, y generan una mayor sensación de bienestar a funcionarios y visitantes. 
</p>

<p>
“Este es un trabajo conjunto y exitoso entre distintas dependencias de la Administración Municipal para entregar a Medellín unas obras que mejoran el aspecto gris de las edificaciones y sus alrededores, cualifican el paisaje con un aspecto colorido y amigable con el medio ambiente, y que incluyen iluminación funcional para conservar las plantas sanas y vitales”, explica Rodrigo Foronda Morales, director general de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–. </p>

<p>
Este proyecto fue planificado, diseñado, implementado, supervisado y administrado por la Agencia APP y financiado con recursos de las secretarías de Infraestructura Física y Suministros y Servicios. La iniciativa cumple con todos los requerimientos enmarcados en el Plan de Desarrollo Medellín Futuro 2020–2023 y la línea de Ecociudad.</p>

<p>
Otros beneficios ambientales de estos jardines verticales son la generación de oxígeno anual para 684 personas y la absorción de 88.920 gr de polvo y de 1.466 Kg de metales pesados por año. </p>



