<h4 text-align="center">

Expertos destacan plataforma de aprovechamiento económico del espacio público de Medellín entre las mejores de Latinoamérica


</h4>

								<hr>


								<ul>
                                    
                                <li> El jurado y los votantes de más de 32 países resaltaron el Sistema gerencial de participación ciudadana como un desarrollo innovador e inspirador en la categoría Mapa Interactivo.  
</li>

<li> La tecnología, adaptada por la Agencia APP para monitorear la gestión del Aprovechamiento Económico del Espacio Público en Medellín (AEEP), sobresalió entre distintas propuestas de Colombia, Ecuador, Panamá y otros países de Latinoamérica.    </li>

<li> El sistema permite procesar grandes volúmenes de datos para tomar mejores decisiones acerca de la ocupación del espacio público con fines comerciales. 
 </li>



</ul>
                                <hr>

								<p>
                                    

								Gracias a las características de lecturabilidad, diseño, interactividad con el usuario e innovación, entre otros aspectos, el Sistema gerencial de participación ciudadana para el monitoreo de la gestión del Aprovechamiento Económico del Espacio Público en Medellín (AEEP) fue premiado entre otras 98 propuestas de Latinoamérica. 
							
<p>


  
Con esta tecnología, la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP– gestiona el uso del espacio público para su aprovechamiento económico y se atienden requerimientos ciudadanos ocho veces más rápido. 


<p>
Asimismo, permite procesar en tiempo real grandes volúmenes de datos sobre  áreas de espacio aprovechadas, solicitudes recibidas por comunas y corregimientos para uso comercial, recaudo mensual por uso del espacio público, zonas de mayor aprovechamiento, el espacio público disponible y ocupado para impulsar la reactivación económica de la ciudad. Además, arroja datos sobre casos de ocupación indebida con el objetivo de definir estrategias para cuidar juntos lo que es de todos.

<p>
El jurado estuvo conformado por un equipo de la empresa estadounidense Environmental Systems Research Institute –ESRI–, líder mundial en el desarrollo y comercialización de software especializado en Sistemas de Información Geográfica, y una votación del público de más de 32 países asistentes. 



<p>
“El Sistema gerencial de participación ciudadana para el monitoreo de la gestión del AEEP es una plataforma tecnológica basada en mapas interactivos, de fácil lectura para el ciudadano, abierta para que todos puedan consultar el aprovechamiento económico del espacio público en la ciudad y puedan ver lo que está pasando en él. Su fácil lectura y administración han permitido que la empresa ESRI, la principal fabricante de tecnologías de geodatabase y sistemas de información geográfica, premie a la Agencia APP y a su plataforma como una herramienta inspiradora para el resto de Latinoamérica. Además, reconocen cómo somos capaces de convertir la información para hacerla más asequible a los ciudadanos”, explicó el subdirector de Gestión de Paisaje y Patrimonio de la Agencia APP, Daniel Madrigal Arango.



<p>
Este reconocimiento se dio en el marco de la conferencia de usuarios ESRI, el evento internacional más grande sobre Sistemas de Información Geográfica, donde se reúnen expertos para socializar nuevas tecnologías, técnicas y actualizaciones sobre un gran número de temáticas, especialmente sobre georeferenciación, que permite identificar los usos del espacio público. 
</p>

<p>
Entre las entidades participantes con iniciativas se encuentran la Universidad Distrital Francisco José de Caldas, la Alcaldía de Santiago de Cali, la Defensoría del Pueblo de Colombia y la Universidad Tecnológica de Panamá. 

</p>

<p>
El AEEP es un instrumento del Plan de Ordenamiento Territorial (POT) (artículo 540) que permite la utilización del espacio público para actividades comerciales de personas naturales y jurídicas, a cambio de una contraprestación económica no tributaria que la Administración Municipal reinvierte en el mantenimiento y recuperación del espacio público.
</p>

<p>

El Sistema gerencial de participación ciudadana para el monitoreo de la gestión del Aprovechamiento Económico del Espacio Público en Medellín, disponible en <a href="https://bit.ly/SistemagerencialAEEP">https://bit.ly/SistemagerencialAEEP</a> promueve la equidad en la regulación del uso y usufructo del espacio público y fortalece a la ciudad como Valle del Software. 

</p>

