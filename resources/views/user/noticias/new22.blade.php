<h4 text-align="center">

Con actividades comerciales, gastronómicas y culturales
avanza la reactivación del Parque Lleras



</h4>

								<hr>


								<ul>
                                    
                                <li> Los establecimientos comerciales del sector han tenido, en promedio, un aumento superior al 46% de sus ingresos desde el inicio de la estrategia.  
</li>

<li> Hasta diciembre de 2021 el Parque Lleras tendrá durante todos los fines de semana una amplia presencia institucional con activaciones artísticas, feria de servicios, festivales, entre otras actividades.     </li>

<li> La oferta de ocio y entretenimiento del parque se complementa con un festival gastronómico, una feria de artesanos, emprendedores y artistas, y nuevos metros cuadrados de aprovechamiento económico del espacio público. 
 </li>



</ul>
                                <hr>

								<p>
                                    

								En el Parque Lleras, el corazón del ocio y del entretenimiento de Medellín, inicia desde este fin de semana y hasta fin de año una nueva estrategia de reactivación económica para acompañar a los comerciantes del sector, fortalecer el turismo responsable, generar nuevos espacios para el disfrute ciudadano y reforzar la presencia institucional.
							
<p>


  
Las actividades, articuladas entre diferentes dependencias de la Alcaldía de Medellín, como la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–, y las secretarías de Gobierno y Desarrollo Económico, comprenden un festival gastronómico con seis food trucks de reconocidas marcas, una feria de artesanos, emprendedores y artistas, con más de 50 participantes y 135 nuevos metros cuadrados de aprovechamiento económico con la extensión de negocio de cuatro establecimientos. 


<p>
“Todos invitados a ir con sus familias, con su pareja y con sus amigos al Parque Lleras. Con nuestra participación en estos eventos estamos aportando un granito de arena para la reactivación de los comerciantes y de esta zona de la ciudad para seguir impulsando el proceso de reactivación económica. Volvamos al Lleras”, expresó el secretario de Gobierno, Esteban Restrepo. 

<p>
El proceso de reactivación del Parque Lleras y Provenza, que ha vinculado a más 100 comerciantes del sector, ha permitido un aumento superior al 46% de los ingresos de los establecimientos, en promedio, desde el inicio de la estrategia; y la generación de 342 empleos directos.



<p>
Para Rodrigo Foronda Morales, director general de la Agencia APP, “la apropiación del Parque Lleras por parte de la ciudadanía es una realidad. Desde la Administración Municipal estamos liderando la generación de nuevos espacios para esta zona. Así estamos promoviendo un nuevo aire y por eso queremos invitar a toda la ciudadanía a que se vuelque en nuestro parque, a que vengan, participen y disfruten de estos espacios que estamos construyendo, de la mano de los comerciantes y de los privados, para el disfrute de toda la ciudadanía”. 



<p>
Otras de las acciones con las que la Administración Municipal busca impulsar la reactivación integral del Lleras son la intervención de 2.125 metros cuadrados de arte urbano temporal por medio de la iniciativa Pinturita MED.
</p>

<p>
“Invitamos a la ciudadanía a disfrutar de comida peruana, italiana, empanadas, hamburguesas y carnes. Vengan a apoyar estos emprendimientos y estas economías que necesitamos que crezcan al lado de las entidades gubernamentales y de la comunidad”, concluyó Juan David Piedrahita, presidente de la Asociación de Food Trucks de Medellín. 

</p>

<p>
Adicionalmente, la Alcaldía de Medellín lidera la mesa permanente de atención a la crisis con gremios, empresarios y comerciantes de El Poblado; el acompañamiento en la implementación de protocolos de bioseguridad, cultura ciudadana de autocuidado y control de aforos; y la vigencia del toque de queda nocturno para menores de edad desde las 7:00 p. m. hasta las 5:00 a. m.. 
</p>


