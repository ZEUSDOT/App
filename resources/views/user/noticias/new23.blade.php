<h4 text-align="center">

Listado de elegibilidad de artistas
Proyecto de Arte Urbano - Punto Cero




</h4>

					
                                <hr>

								<p>
                                    

								El intercambio vial de Punto Cero es un lugar idóneo para continuar enriqueciendo el paisaje de la ciudad con prácticas artísticas de arte urbano. Es, además, un referente del territorio por sus características locativas y de impacto en la movilidad y en la circulación de la ciudadanía y, recientemente, se posicionó como espacio de la expresión social y en un punto de conexión con la calle Barranquilla y su galería de arte urbano a cielo abierto.
							
<p>


  
Bajo estas premisas, la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–, ente descentralizado de la Alcaldía de Medellín, nos contrató como curadores para ser parte del proceso de investigación, conservación, selección de artistas y crew´s que, por sus cualidades artísticas, pueden asumir el reto de intervenir los espacios del viaducto con propuestas nuevas o con la actualización de piezas existentes. 


<p>
Como antecedente importante, la Agencia APP conformó, por medio de una invitación a través de la página web y de las redes sociales, un banco de portafolios de artistas locales para tener un estado del arte y una aproximación estética y conceptual de los creadores locales de diferentes disciplinas de la creación.

<p>
Con la conformación de este banco de portafolios y de acuerdo con la naturaleza del sector a intervenir, como equipo curatorial establecimos cinco pautas para la selección de artistas, crew´s y colectivos que participarán del proyecto de arte urbano Punto Cero con producción de piezas en las técnicas de grafiti y muralismo. Estos conceptos son:

<p>

<ul>
    <li type="circle">
	<b>Participación en proyectos recientes de la Agencia APP </b> 
    Como una manera de ampliar el alcance a la comunidad queremos dar oportunidad de intervención a artistas, escritores y colectivos que no hayan estado presentes o que han tenido una participación mínima en los proyectos ejecutados por la entidad, entre ellos, Parque Lleras, Provenza y la calle Barranquilla.
    </li>
    <br>
    <li type="circle">
    <b>Trayectoria en el arte urbano gráfico</b> Procesos destacados con acciones permanentes en el espacio público.
    </li>
    <br>
    <li type="circle">
<b>Dominio de técnicas del grafiti y/o del muralismo en gran formato</b> Nivel de competencia en el control de grandes superficies y el logro de objetivos técnicos con diversas herramientas en el contexto del arte de calle. 

    </li>
    <br>
    <li type="circle">
<b>Aspectos conceptuales evidenciados en la obra</b> Capacidad de representar, a través de sus propuestas gráficas, procesos y pensamiento crítico con elementos compositivos que integren investigación, contenidos creativos, originalidad y alto nivel de impacto visual.
    </li>
<br>
<li type="circle">
<b>Procesos socioculturales y reflexivos evidenciados en su intención artística</b> Compromiso y coherencia del artista o del colectivo con los contextos sociales, ambientales e históricos, tanto desde el ámbito compositivo de las obras, como en las acciones permanentes para involucrar comunidades o nichos poblacionales en la experiencia artística. 
</li>

</ul>

<p>
Consideramos que estos criterios son pertinentes para seleccionar algunos artistas y colectivos que asuman el enriquecimiento visual de Punto Cero con la creación de sus obras urbanas. Y, como complemento, es importante anotar que el lugar presenta a la fecha algunas intervenciones gráficas que tenemos registradas y que, como se hizo en la calle Barranquilla, haremos el esfuerzo por contactar a los autores y autoras para invitarlos al ejercicio de renovación.   
</p>

<p>
Como equipo curatorial, que actualmente prestamos nuestros servicios a la Agencia APP y después de recibir 46 portafolios de diversa índole, aclaramos que la revisión de los mismos la realizamos respetando la diversidad de estilos y propuestas, valorando el crecimiento del arte público de Medellín y la riqueza que evidencia el talento de sus ciudadanos. 
</p>

<p>
Agradecemos a todos los artistas y colectivos que participaron y, en especial, al gremio de arte urbano que con sus acciones fortalecen las prácticas callejeras y contribuyen al fortalecimiento visual de nuestros muros. 
</p>

