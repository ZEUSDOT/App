<h4 text-align="center">

Convenio de la Agencia APP y el Politécnico Colombiano Jaime Isaza Cadavid buscará la ampliación de la ciudadela educativa del sur de la institución



</h4>

								<hr>


								<ul>
                                    
                                <li> La institución universitaria y la Agencia APP buscan fortalecer las relaciones institucionales y estrechar los lazos de cooperación alrededor de las buenas prácticas. 
 
</li>

<li> La formulación de proyectos, el manejo integral del espacio público y la investigación serán otros ejes de trabajo entre ambas entidades.     </li>





</ul>
                                <hr>

								<p>
                                    

								La generación de alianzas público privadas para gestionar y ejecutar iniciativas de alto impacto para la ciudad de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP– y la capacidad investigativa y la promoción del desarrollo tecnológico del Politécnico Colombiano Jaime Isaza Cadavid, son algunas de las razones que ambas entidades identificaron para firmar un convenio marco de cooperación que busca el intercambio de experiencias y la transferencia de conocimientos.
							
<p>


  
Asimismo, se busca la repotenciación y ampliación de 2.711 metros cuadrados del bloque P17 de Ingenierías del Politécnico. Este edificio cuenta hoy con 44 aulas y seis servicios sanitarios. Con el proyecto, se busca pasar de tres a cinco niveles y sumar 20 salones, diez salas de estudio, una oficina y un aula múltiple para 100 personas.


<p>
“Con este importante convenio reunimos esfuerzos para sacar adelante los objetivos de ambas instituciones. Inicialmente, identificar posibilidades de alianzas público privadas para la construcción y renovación urbana de la plazoleta central y del bloque de ingenierías de la ciudadela sur. Posteriormente, impulsaremos el desarrollo de saberes científicos, arquitectónicos y financieros para seguir materializando y construyendo la Medellín Futuro y contribuir al fortalecimiento de la educación en Antioquia”, explicó el director general de la Agencia APP, Rodrigo Foronda Morales. 

<p>
El convenio también comprende el aprovechamiento de las instalaciones físicas de ambas partes como aulas, laboratorios, bibliotecas e instalaciones y la participación conjunta en proyectos de investigación, extensión, cursos, entre otros. 




<p>
Para el rector del Politécnico Colombiano Jaime Isaza Cadavid, Libardo Álvarez Lopera: “nuestro gran propósito con la Agencia APP es renovar la ciudadela sur de la institución universitaria y realizar obras complementarias en la sedes de Rionegro, Marinilla, San Jerónimo y Urabá. Este convenio, además de la infraestructura, también comprende intercambio de conocimiento científico y tecnológico, investigación aplicada, uso de instalaciones y trabajo en equipo con personal calificado. Con la Agencia APP haremos historia para el desarrollo de la ciudad”. 



<p>
Este convenio, que tendrá una vigencia de dos años, propenderá por el desarrollo sostenible de las instituciones, buscará el beneficio de la comunidad estudiantil y la ciudadanía, y se consolida como un ejercicio de colaboración pública en la construcción de Medellín Futuro.
</p>

<p>
La Agencia APP avanza en la actualidad en distintas iniciativas públicas y privadas como la modernización de la Unidad Deportiva Estadio Atanasio Girardot, el Centro Cívico, el Centro de Detención Preventivo para Sindicados, el Arena Medellín y el Hotel Plaza Mayor.
 

</p>



