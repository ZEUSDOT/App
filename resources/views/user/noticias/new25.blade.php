<h4 text-align="center">
En el aniversario 346 de Medellín, el corazón del Centro se llena de luz para celebrar la vida

</h4>

								<hr>


								<ul>

                                <li>Desde su fundación, el 2 de noviembre de 1675, la capital antioqueña ha tenido un constante desarrollo gracias al Ferrocarril de Antioquia, a las Empresas Públicas de Medellín (EPM), al Metro y al permanente desarrollo industrial. </li>
                                    
                                <li> En esta fecha, el emblemático edificio de La Naviera será iluminado a las 6:00 p. m. para destacar sus cualidades arquitectónicas y contribuir con la recuperación integral del Centro tradicional.  
 
</li>

<li>  También se le entregará a la ciudad la nueva Galería Urbana a Cielo Abierto de la calle Barranquilla y la transformación urbana de la carrera Palacé, en el barrio Prado.    </li>





</ul>
                                <hr>

								<p>
                                    

								Este miércoles, 3 de noviembre, Medellín conmemora 346 años de haber sido fundada por el gobernador Miguel de Aguinaga, en 1675, cuando recibió el nombre de Villa de la Candelaria y desde esta fecha ha vivido un continuo proceso de transformación y de planificación urbana que le ha permitido constituirse actualmente en un Distrito de Ciencia, Tecnología e Innovación y ser un referente para el país
<p>


Uno de los símbolos del crecimiento industrial y empresarial de Medellín es el edificio La Naviera y, a propósito de esta fecha, la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP le entregará a la ciudad, desde las 6:00 de la tarde, en Plaza Botero, la nueva iluminación arquitectónica de este emblemático edificio.

<p>
“La Agencia APP tiene dos funciones esenciales: la primera, la estructuración de alianzas público privadas. La segunda, la puesta en valor del patrimonio arquitectónico, el embellecimiento y la cualificación del paisaje urbano. En el marco del 346 aniversario de Medellín, la Agencia APP le hace un regalo a toda la ciudadanía: la entrega del edificio La Naviera con su fachada renovada y con una iluminación arquitectónica. Así ratificamos nuestros objetivos de embellecer la ciudad y poner en valor el patrimonio de una ciudad próspera, grande y abierta para todos”, afirmó el director general de la Agencia APP, Rodrigo Foronda Morales. 


<p>
Para rescatar el valor arquitectónico del edificio La Naviera, la Agencia APP realizó el mantenimiento de los 2.910 metros cuadrados de fachada, adecuaciones locativas en 480 metros cuadrados para la operación de las oficinas de la Subdirección de Gestión del Paisaje y del Patrimonio de la entidad y la instalación de, aproximadamente, 1.700 nodos de luces LED con capacidad para generar hasta 16.000 colores y 700 escenarios para festejar fechas especiales. En estas acciones se invirtieron $1.392 millones. 



<p>
El edificio La Naviera, ubicado entre las avenidas Primero de Mayo y Palacé, se destaca por sus redondeadas formas que simulan la proa de un barco y fue referente de la arquitectura moderna en Medellín. Fue construido entre 1946 y 1949 por la oficina de arquitectura Viera, Vázquez y Dotheé como sede oficial de la empresa de navegación fluvial, fundada en 1920 por inversionistas antioqueños, que entró en quiebra en 1954. Desde entonces ha tenido distintos habitantes como Rentas Departamentales, la Fiscalía y, finalmente, fue entregada al Alma Máter de Antioquia. 


<p>
Como parte de la conmemoración del aniversario de la ciudad se realizará un concierto a cargo del Quinteto Antioqueño de Cuerdas, a las 6:00 p. m., en Plaza Botero. Además serán entregados 3.200 metros cuadrados de arte urbano realizado por 27 artistas y colectivos de artistas seleccionados a través del banco de portafolios, y más de 20 artistas y colectivos urbanos convocados por su actividad constante en el sector en la Galería Urbana a Cielo Abierto de la calle Barranquilla; así como la cualificación del paisaje urbano del barrio patrimonial Prado, gracias al mantenimiento arquitectónico de 109 predios y a la instalación de iluminación arquitectónica para resaltar sus atributos patrimoniales.
</p>

<p>
Adicionalmente, la Administración Municipal hará desde las 7:00 p. m. de este miércoles, 3 de noviembre, un homenaje a la tradición silletera declarada Patrimonio Cultural e Inmaterial de la Nación. Este evento se transmitirá por Telemedellín, desde el Parque de los Deseos, y será un espacio de reflexión y de fortalecimiento de la identidad de la cultura medellinense y de salvaguarda en la celebración del cumpleaños de Medellín. 

 

</p>

<p>“Medellín es Distrito de Ciencia, Tecnología e Innovación y sigue creciendo y cumpliendo años, y los vamos a celebrar también el 2 de noviembre a las 7 de la noche en el Teatro Ateneo Porfirio Barba Jacob con el lanzamiento de dos documentales: Nuestro patrimonio  inmaterial silletero será reconocido a través del Desfile de Silleteritos que se hizo este 2021 y con el documental de cómo fue volver a las calles después de vivir la pandemia y no haber tenido un desfile presencial”, dijo el secretario de Cultura Ciudadana, Álvaro Narváez. 
</p>



