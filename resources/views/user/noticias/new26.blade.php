<h4 text-align="center">
Con La Naviera, la Galería Urbana a Cielo Abierto y Palacé se renueva el paisaje urbano de Medellín 

</h4>

								<hr>


								<ul>

                                <li>Con estas tres intervenciones alrededor de la puesta en valor del patrimonio y del arte urbano, la Administración Municipal aporta a la recuperación del Centro tradicional y a la reactivación económica de la ciudad.    </li>
                                    
                                <li>1.700 nodos LED tendrán la capacidad de generar hasta 700 escenarios de luz y color para celebrar fechas especiales en la fachada del edificio patrimonial La Naviera. 
 
</li>

<li>  Con un evento protocolario, la entrega de las obras se realizó en la Plaza Botero, con la participación del alcalde de Medellín, Daniel Quintero Calle, y la presentación musical del Quinteto Antioqueño de Bronces.   </li>





</ul>
                                <hr>

								<p>
                                    

								Desde este miércoles, 3 de noviembre, Medellín cuenta con tres nuevas intervenciones urbanas para apreciar y recorrer. En la comuna 3-Manrique, la nueva Galería Urbana a Cielo Abierto de la Calle Barranquilla expone diversas manifestaciones de color, firmas y grafitis; y en la comuna 10-La Candelaria, el edificio La Naviera ilumina el corazón del Centro de Medellín y la transformación de la carrera Palacé pone en valor las fachadas de grandes edificaciones. 
<p>


Las tres obras le apuestan a la conservación del patrimonio, a la generación de mayor presencia institucional, a la apropiación social de los lugares y a la reactivación económica de la ciudad a través de la promoción del turismo y el apoyo a artistas y colectivos de arte urbano. 

<p>
“La pandemia, de alguna forma, por lo dolorosa que fue también fue un llamado a mirar con unos nuevos ojos, volver a querer. Los niños son maestros en eso, porque ellos ven todo con una curiosidad impresionante, para ellos todo es nuevo y para ellos La Naviera por la que pasamos tantas veces es nueva y habla de la historia de esa Medellín Futuro”, expresó el alcalde Daniel Quintero Calle.


<p>
La Naviera, edificio ubicado entre las avenidas Primero de Mayo y Palacé, es un Bien de Interés Cultural que se destaca por sus redondeadas formas que simulan la proa de un barco. Ahora, la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas, lo puso en valor a través de acciones de mantenimiento y lavado en 2.910 metros cuadrados de fachada; la instalación de 1.700 nodos LED con capacidad para generar hasta 16.000 colores y 700 escenarios para celebraciones especiales; y adecuaciones locativas de 480 metros cuadrados para la operación de las oficinas de la Subdirección de Gestión de Paisaje y Patrimonio de la Agencia APP. 
 



<p>
“La Agencia APP está empeñada en resaltar nuestros bienes culturales, que la ciudadanía se acerque a ellos debido al gran contenido histórico y cultural que representan para los habitantes de la ciudad. Es por eso que hemos invertido esfuerzos y recursos en recuperar los bienes que queremos que vean, quieran y estén cerca de ellos”, dijo el director general de la Agencia APP, Rodrigo Foronda Morales.


<p>
Asimismo, en el barrio Prado, se hizo entrega oficial de las obras de cualificación y puesta en valor del patrimonio en 33.940 metros cuadrados de fachadas intervenidas con pintura, en alianza con la Fundación Pintuco, y de 28 bienes de interés cultural iluminados para destacar sus cualidades arquitectónicas e históricas. 

</p>

<p>
Esta iluminación sobre la carrera Palacé, en Prado, considera aspectos como el color de la luz, la intensidad, el direccionamiento, los materiales, la forma y las alturas de los inmuebles para aportar al embellecimiento y revitalización de la vía y la conservación del patrimonio, así como incentivar nuevas dinámicas económicas relacionadas con el arte, la cultura y el turismo. 

 

</p>

<p>Muy cerca de esta vía también se entregó la nueva Galería Urbana a Cielo Abierto de la Calle Barranquilla, ubicada entre Manrique y Prado, pero con impacto en otros barrios como Lovaina y Sevilla. 
</p>

<p>
Esta fue una actividad que contó con la participación de 27 artistas y colectivos de artistas seleccionados a través del banco de portafolios y más de 20 artistas y colectivos urbanos convocados por su actividad constante en el sector. También comprendió la realización de microresidencias artísticas para comprender mejor las dinámicas de la zona y 3.406 metros cuadrados de arte urbano que incluyen fachadas, medianeros, culatas y viaductos. 

</p>

<p>
Con esta galería, además de la de El Poblado y del nuevo tramo del Grafitour, Medellín impulsa la reactivación económica del sector artístico y del turismo. 

</p>


