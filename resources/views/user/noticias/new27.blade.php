<h4 text-align="center">
San Ignacio y Prado ADN

</h4>

								<hr>


								<ul>

                                <li>Los dos perímetros declarados están conformados por 54 hectáreas que se expanden alrededor del Conjunto Arquitectónico San Ignacio, y 74 manzanas y 265 Bienes de Interés Cultural (BIC) en el barrio Prado.   </li>
                                    
                                <li>La estrategia fortalece las dinámicas económicas, culturales, patrimoniales y educativas de ambas zonas.
 
</li>

<li> Con estas, Medellín ya tiene tres Áreas de Desarrollo Naranja: el Territorio Creativo del Perpetuo Socorro, el Territorio Cultural y Educativo en San Ignacio, y el Territorio Patrimonial de Prado.   </li>





</ul>
                                <hr>

								<p>
                                    

								Este viernes 26 de noviembre, el Ministerio de Cultura y la Alcaldía de Medellín declararon al territorio Cultural y Educativo - San Ignacio y al territorio Patrimonial - barrio Prado como Áreas de Desarrollo Naranja (ADN) para la ciudad, declaratoria que incentiva la apropiación del espacio público, aporta a la protección del patrimonio material e inmaterial, fortalece la actividad creativa, cultural y educativa e incrementa el sentido de pertenencia del territorio por parte de la comunidad.  
<p>


“El principal talento que tiene Medellín es su gente, es el activo más importante. Uno todos los días se sorprende de la creatividad, cómo los emprendedores tienen nuevas formas para generar soluciones a través del sector creativo (…) nosotros estamos comprometidos con la estrategia de desarrollo naranja”, expresó el secretario de Desarrollo Económico, Alejandro Arias.
 

<p>
El perímetro seleccionado en San Ignacio está conformado por 54 hectáreas que se expanden alrededor del corazón del proyecto que es el Conjunto Arquitectónico San Ignacio, conformado por la Plazuela e Iglesia San Ignacio, Claustro Comfama, Paraninfo y Antigua Escuela de Derecho de la Universidad de Antioquia y que alberga cuatro barrios de la comuna 10: Boston, La Candelaria, Bomboná y Colón.



<p>
Para la ministra de Cultura, Angélica María Mayolo Obregón, esto representa un hito importante para la ciudad y para el país. “Esto facilita muchos instrumentos para que podamos tener inversiones futuras en el territorio, pensando en la cultura como eje central, porque la voluntad decidida de los mandatarios es lo que hace posible que la oferta de servicios llegue a los terrirorios”, manifestó la Ministra.    
 



<p>
Este es un territorio con 200 años de trayectoria y vocación cultural, creativa y educativa, donde el trabajo continuo de actores culturales, teatros, bares, instituciones educativas, industrias creativas, oficios de tradición, entre otros, aportaron a su reconocimiento como Distrito.


<p>
“Para nosotros los artistas ser parte del Distrito San Ignacio es algo que nos motiva, porque aparte de pertenecer, es ser parte de una alianza con otros teatros y hacer que a las personas les resulte más llamativo venir al centro y participen de toda la programación cultural que tenemos desde la danza, el teatro, la comedia. Ser parte del Distrito es realmente un orgullo”, explicó la directora del teatro Casa Clown, Sara Suárez.


</p>

<p>
El Área de Desarrollo Naranja (ADN) del barrio Prado, ubicada en el costado norte del centro tradicional de Medellín, comprende 74 manzanas y 265 Bienes de Interés Cultural (BIC). En el área de influencia directa se ubican equipamientos y espacios para la educación y la innovación de gran importancia como la Universidad de Antioquia y Ruta N. 


 

</p>

<p>“Esta declaratoria va a permitir que lleguen inversiones responsables con el patrimonio. Este espacio le va a permitir a la ciudad tener una zona más donde la cultura se siga moviendo”, dijo el secretario de Cultura Ciudadana, Álvaro Narváez.
</p>

<p>
Este ADN opera como un centro de actividad económica y creativa, contribuye a la renovación urbana y fortalece el emprendimiento, el empleo basado en la creatividad, el turismo, la puesta en valor del patrimonio cultural, la conservación medioambiental y la transferencia de conocimientos para el futuro desarrollo del sector. 

</p>

<p>
Asimismo, el barrio tiene actores culturales que realizan actividades como el teatro, la danza, las artes visuales, el turismo cultural, la producción de artesanías y la educación artística.

</p>

<p>
“Vemos desde la Corporación grandes ventajas y oportunidades desde el ADN como un aporte a la recuperación del tejido social, la incorporación de otras dinámicas en el territorio y la explotación y de la diversidad artística y cultural”, dijo la directora administrativa de la Casa del Teatro de Medellín, Clara Pabón Duarte. 
</p>

<p>
El desarrollo urbano de Prado se orienta, desde 2017, bajo los principios del Plan Integral de Gestión de Prado, estrategia liderada por la Alcaldía de Medellín y la Agencia APP con acciones como la transformación de la carrera Palacé a través de la luz dirigida que destaca las características arquitectónicas de los Bienes de Interés Cultural (BIC) para resaltar su valor; la recuperación del Parque Olano como ícono y referente de la memoria de Prado y la nueva Galería Urbana A Cielo Abierto de la Calle Barranquilla con expresiones artísticas que reflejan la cultura, la vecindad, el barrio y el homenaje a personajes locales.

</p>

<p>
Las Zonas ADN en los territorios: Patrimonial (barrio Prado) y Cultural y Educativo (sector de San Ignacio), se suman a la que fue inaugurada en noviembre de 2020 en el territorio Creativo (barrio Perpetuo Socorro). 
</p>

<p>
En estos puntos se gestan redes, nodos, clústeres y otras formas de asociatividad que recogen las expresiones culturales, artísticas y de emprendimiento para el fortalecimiento de la economía creativa que en Medellín representa el 3 % del BIP, gracias a que este sector alberga 6.000 empresas formales que generan 33.000 empleos.
</p>

<p>
Los colectivos y organizaciones que tienen presencia en los tres territorios han aprendido a aprovechar sus potencialidades y la riqueza con la que cuentan, pues allí existen más de 75 espacios culturales y están ubicados por lo menos 28 sitios de interés histórico y más de 261 bienes inmuebles declarados como patrimonio cultural local.

</p>

<p>
La consolidación del Perpetuo Socorro, San Ignacio y Prado como Áreas de Desarrollo Naranja obedece a lo establecido en el Plan de Desarrollo Medellín Futuro 2020-2023 y se fundamenta en el Acuerdo Municipal 119 de 2019 que fomenta la articulación, el impulso, el reconocimiento, la formalización y la vinculación de las economías creativas a las dinámicas económicas. 
</p>


