<h4 text-align="center">
Fue entregada oficialmente la intervención artística del Puente de la 4 Sur que enaltece la tradición textilera de Medellín

</h4>

								<hr>


								<ul>

                                <li>Se intervinieron 1.166 metros cuadrados y 2.015 metros lineales con la participación de seis artistas urbanos. </li>
                                    
                                <li>La renovación pictórica en el puente desarrolló el concepto Trenzados con el que reconoce a los empresarios y operarios que desde hace más de un siglo impulsan desde sus textileras el progreso local.

 
</li>

<li> Este espacio mejora y cualifica el paisaje urbano, la consolidación de Medellín como una galería de arte abierta para el disfrute de todos y el apoyo a la reactivación económica del sector de artistas urbanos. </li>





</ul>
                                <hr>

								<p>
                                    

								La Alcaldía de Medellín entregó de manera oficial las obras de intervención artística del puente Gilberto Echeverri Mejía, conocido como el Puente de la 4 Sur, con base en lo que define el Acuerdo Municipal 010 de 2020 en el que se establece como objetivo promocionar la realización de intervenciones y actividades de alto nivel, fomentando el respeto al paisaje, el patrimonio, el medio ambiente y el espacio público.  
<p>


Durante la entrega se presentó el artista afro Bomby, uno de los artistas musicales más reconocidos de la comuna 13-San Javier, y hubo un show de baile de Nexo Klan y Klandestinos Krew, dos agrupaciones de baile del sector de Guayabal. 
 

<p>
La intervención artística fue llevada a cabo por la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP– junto a artistas de la ciudad y fue inspirada en la tradición textilera de Medellín que tiene más de un siglo, cuando empresas como la Fábrica de Hilados y Tejidos del Hato S. A. (Fabricato), la Compañía Colombiana de Tejidos (Coltejer) y Tejidos el Cóndor (Tejicondor) impulsaron y posicionaron la industria textil local y de la región como claro ejemplo de la templanza y tradición empresarial antioqueña.




<p>
“Este es el principio de otros sueños que tenemos en Prado Centro, La Alpujarra, en otros territorios de la ciudad que creemos que necesitan más luz que le da mucha vida y color. Este es un homenaje para todas esas madres cabeza de familia que tienen talleres de confecciones y generan empleo y sacan sus hijos adelante por medio del sector textil”, expresó la gestora social de Medellín, Diana Osorio.  
 



<p>
Se intervinieron 1.166 metros cuadrados y 2.015 metros lineales con la participación de seis artistas urbanos y un equipo logístico, de siete personas más, encargadas de la seguridad ocupacional y del trabajo social.



<p>
“Estoy muy contento de haber terminado este proyecto tan grande que fue pintar el Puente de la 4 Sur. Agradezco la invitación a este proyecto, con un equipo diverso. Fue un proceso muy lindo”, manifestó Jhonatan Carvajal, artista plástico de la Universidad de Antioquia.



</p>

<p>
Con su concepto Trenzados, la intervención pictórica en el Puente 4 Sur hace un reconocimiento a empresarios, operarios, técnicos y demás personas que hilando en sus fábricas y talleres han forjado  una de las idiosincrasias más significativas de la región y que son sinónimo de trabajo entrelazado, de unidad y armonía de hilos que, indistintamente, han generado unos entramados sociales que potencian los pilares de la sociedad.
 


 

</p>

<p>“Una de las misiones esenciales de la Agencia APP es llenar la ciudad de luz, de arte, de magia, de color. Es por eso que estamos entregando la obra Trenzados, un homenaje que le hace la Alcaldía de Medellín a todo nuestro sector empresarial textil que desde los años 40 y 50 ha forjado nuestra pujante raza antioqueña”, explicó el director general de la Agencia APP, Rodrigo Foronda Morales.  
</p>

<p>
Trenzados utiliza la forma de las columnas y los cables de tensión para articularlos en una propuesta visual que proyecta la fuerza de la unidad de estos hilos de color para profundizar en la necesidad de la unión y tejer desde abajo las columnas del progreso.
 

</p>

<p>
Con la intervención artística se mejora y cualifica el paisaje urbano, la movilización de los procesos culturales, la diversificación de las manifestaciones de arte urbano con diferentes estilos y formas, el acompañamiento en la reactivación económica del sector de artistas y colectivos de arte urbano, el posicionamiento de un ícono de ciudad a través del arte urbano y la consolidación de Medellín como una galería de arte abierta para el disfrute de todos.


</p>

<p>
De los 14.300 metros establecidos en el Plan de Desarrollo Medellín Futuro 2020-2023, la capital antioqueña alcanza una ejecución de más de 8.000 metros cuadrados de arte urbano: la Galería Urbana a Cielo Abierto de la Calle Barranquilla (en el nororiente), la fachada de la sede administrativa de la Agencia APP, el intercambio vial Bulerías, las obras en el Parque Lleras y en Provenza, y en los puentes de la Avenida 33, la Avenida Guayabal, la calle Colombia y en el barrio Caribe a la altura de la Secretaría de Movilidad.
</p>



