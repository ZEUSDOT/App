<h4 text-align="center">
Con 80 fachadas intervenidas se renueva el barrio Prado Centro 	

</h4>

								<hr>


								<ul>

                                <li>Son más de 200 personas, entre propietarios y residentes, quienes disfrutan de la puesta en valor de las propiedades.   </li>
                                    
                                <li>Las obras para mejorar el paisaje del único barrio patrimonial de Medellín se realizan en la carrera 49 (Venezuela) y la calle 65 (Jorge Robledo).

 
</li>

<li> Esta iniciativa contribuye con el mejoramiento integral del entorno y atrae nuevas dinámicas comerciales y turísticas.    </li>





</ul>
                                <hr>

								<p>
                                    

								Con más color y más cuidado en las fachadas, los alrededores del Parque Olano, en el barrio Prado, lucen una nueva apariencia, gracias a las intervenciones que adelanta la Alcaldía de Medellín en cerca de 80 de ellas y en más de 3.500 metros cuadrados en la carrera Venezuela y la calle Jorge Robledo. 
<p>


Estos trabajos, que generan 26 empleos, mejoran el entorno y les devuelven el valor arquitectónico a las propiedades, contribuyen con el mejoramiento integral del barrio, además que atraen más turismo y actividades comerciales y culturales, en el marco del Plan Integral de Gestión de Prado.
 

<p>
“La intervención que estamos realizando en el sector es de suma importancia para nuestro barrio patrimonial para darle, nuevamente, un aire turístico y atractivo para la ciudadanía, que todos volvamos a conectarnos con él. Asimismo, y lo más importante, ha sido una conversación con todos los moradores del sector para acordar con ellos el tipo de intervención, el color y las especificaciones técnicas”, explicó el director general de la Agencia APP, Rodrigo Foronda Morales.




<p>
Aunque es reconocido como el Parque Olano, sus dimensiones, de aproximadamente 46 metros de largo y 22 metros de ancho, responden más a las características de una plazoleta. Este parque lleva el apellido Olano en honor a Don Ricardo Olano, un hombre insigne de la ciudad, con gran trayectoria como líder de la Sociedad de Mejoras Públicas de Medellín y uno de los fundadores e impulsores del barrio Prado. 
 



<p>
El comerciante y empresario, Don Ricardo Olano, no solamente lideró el urbanismo del barrio, también promovió la siembra de guayacanes amarillos y rosados. Precisamente, Olano escribió en sus memorias su anhelo de que cuando estos árboles florecieran fueran una mancha de oro en toda la zona. 



<p>
Para Eduardo Monzón, residente y beneficiario, “es óptimo que se ponga mano, y que esta zona, que es de historia y parte de la memoria de la ciudad, no se deje olvidar y se rescate. Paisaje y patrimonio, amor a lo que es propio; conservar lo que se ha de conservar”. 



</p>

<p>
Estas obras se complementan con otras acciones de la Agencia APP, como el mejoramiento de fachadas sobre la carrera Palacé, la iluminación arquitectónica de Bienes de Interés Cultural, el mejoramiento del espacio público, el nuevo Parque Prado construido por la Secretaría de Infraestructura y la reciente declaratoria de Área de Desarrollo Naranja que promoverá la creación y fortalecimiento de las industrias creativas.
 


 

</p>

