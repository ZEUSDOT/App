<h4 text-align="center">
Arena Medellín avanza para entregar a la ciudad un centro de espectáculos de talla mundial
</h4>

								<hr>


								<ul>
                                    
                                <li>Será una estructura multipropósito con capacidad hasta para 16.000 espectadores. 
</li>

<li>La etapa de prefactibilidad analizó variables técnicas, jurídicas, financieras, estratégicas y comerciales del proyecto, incluyendo el diagnóstico de los predios y la infraestructura existente.  </li>

<li>Arena Medellín estará ubicada en el estadio Cincuentenario, y su cancha auxiliar, y la inversión inicial ascendería a $162 mil millones aproximadamente. </li>


</ul>
                                <hr>

								<p>
                                    
                                La Alcaldía de Medellín da un paso definitivo en el sueño de entregarle a la ciudad un centro de espectáculos de talla mundial y con las características técnicas, de infraestructura y logísticas para conciertos, ferias, congresos, charlas y convenciones, entre otros eventos.
                                </p>
							
<p>
Durante el desarrollo de la etapa de prefactibilidad se identificaron factores sociales, ambientales, prediales y ecológicos que contribuyen a las apuestas estratégicas de la ciudad. Así, luego de revisar el esquema técnico, jurídico y financiero, el comité evaluador, liderado por la Secretaría de Desarrollo Económico y la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–, determinó la viabilidad de continuar con la fase de factibilidad.
<p>
En la siguiente etapa, la de factibilidad, el originador del proyecto realizará los estudios a nivel de fase II en los componentes técnicos, jurídicos, ingenieriles y financieros. 
<p>
Para el director general de la Agencia APP, Rodrigo Foronda Morales: “en esta segunda etapa habrá un mayor detalle de las características arquitectónicas, financieras, jurídicas y estratégicas. En esa etapa veremos el detalle del diseño y estaremos a punto para entregarle a Medellín Futuro el recinto de eventos que la ciudad se merece”.</p>

<p>
Arena Medellín contará con una capacidad de hasta 16.000 espectadores, locales comerciales y celdas de parqueo para mejorar la oferta de servicios en el sector, la experiencia, comodidad y tranquilidad de los asistentes.
<p>
Esta iniciativa es una APP (Asociación Público-Privada) de iniciativa privada que continuará realizando el diseño, la construcción, la financiación, la operación y el mantenimiento de la infraestructura por el plazo de la concesión. 
<p>
La inversión para la construcción y operación del Arena Medellín sería de $162 mil millones aproximadamente en valores constantes, es decir, sin variaciones por cambio del precio en el tiempo. 
<p>
Arena Medellín tendrá las características necesarias en capacidad, iluminación, seguridad y sonido para entregarle a Medellín un escenario con las mejores condiciones para grandes eventos nacionales e internacionales. Además, junto con espacios como el Parque Explora, el Parque Norte, el Jardín Botánico y el Parque de los Deseos, consolidarán el norte como un foco de desarrollo urbano y social.
</p>
<p>
Se espera que, en cerca de nueve meses, la Agencia APP reciba por parte del originador del proyecto los estudios de factibilidad para avanzar con su evaluación y dar paso a las demás aprobaciones del proyecto que permitan adjudicar y comenzar la construcción del Arena Medellín. 
</p>

