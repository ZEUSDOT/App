<h4 text-align="center">
La red Metrópolis congrega a nivel global más de 140 grandes ciudades y áreas metropolitanas

</h4>

                                <hr>

								<p>
                                    

								La red Metrópolis congrega a nivel global más de 140 grandes ciudades y áreas metropolitanas y su objetivo es servir como plataforma para el intercambio de conocimiento en diversos ámbitos en materia de gobernanza urbana entre sus ciudades miembro. Medellín, hace parte de esta red desde hace cerca de 10 años, y en el 2021, la ciudad fue invitada junto con Bruselas-Región Capital, Bélgica, Área Metropolitana de Barcelona (AMB), España, Montreal, Canadá  y Seúl, Corea del sur  a participar en el grupo de trabajo para discutir sobre el futuro de los espacios públicos metropolitanos, centrándose en cómo las políticas integradas y transversales pueden generar espacios públicos de mejor calidad, inclusivos, resilientes y sostenibles reconociendo los retos que plantea la comparación de diez espacios públicos a distintas escalas, donde los conceptos tienen significados distintos en contextos diferentes.
<p>


Para esto, cada ciudad participante tuvo la oportunidad de contribuir en los distintos talleres con el intercambio de conocimientos y experiencias, partiendo de un estudio comparativo con estos indicadores mencionados anteriormente, iniciando con la presentación de los proyectos considerados por cada ciudad, seguido de un análisis individual de los mismos a partir de estos indicadores y así su discusión de aprendizajes y conclusiones, permitiendo a todos tener acceso de primera mano a los contenidos y objetivos de cada proyecto y como se abordan las diferentes situaciones que se presentan sobre el espacio público en cada ciudad. 

<p>

En el caso de Medellín, la Agencia APP participa los proyectos de cualificación del paisaje urbano y mejoramiento del espacio público realizados en la Comuna 10, Parque Bolívar, y Comuna 11 - Corredor Avenida Jardín, proyectos que permitieron compartir la experiencia integral desde su concepción hasta su construcción como espacios públicos de calidad, inclusivos, resilientes y sostenibles. También, se dio la oportunidad de presentar otros proyectos de la Agencia que aportaron a estas discusiones de los indicadores.


<p>
Estos talleres concluyen con una publicación que busca inspirar y sentar el camino de las zonas metropolitanas en la creación de mejores espacios públicos para todos, proporcionando herramientas, procesos y recomendaciones de planificación urbana, donde se puede concluir que estas proyecciones de ciudad y regiones están centrando su atención en volver a los componentes esenciales de nuestras comunidades: las personas, los lugares y la naturaleza. 

