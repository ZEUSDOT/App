<h4 text-align="center">
Con la publicación del proyecto de pliegos, comienza la modernización de la Unidad Deportiva Atanasio Girardot 

</h4>

                                <hr>

                                <ul>
                                    
<li>- Con el inicio de obras en 2023 se remodelarán 283.600 metros cuadrados.</li>
    
<li>- La renovación incluirá la construcción de un hotel deportivo y un edificio multipropósito.</li>

<li>- La capital antioqueña se consolidará como un escenario de talla mundial.</li>
    
    
    
    </ul>
    <hr>

<p>La Alcaldía de Medellín, a través de la Agencia para la Gestión del Paisaje el Patrimonio y las Alianzas Público Privadas - Agencia APP –, publicó el proyecto de pliegos para la modernización de la Unidad Deportiva Atanasio Girardot, luego de agotar dos etapas de estructuración como la prefactibilidad y factibilidad. Con el proyecto de pliegos de condiciones de licitación pública inicia la materialización del sueño que hace muchos años tenía Medellín de contar con una Unidad Deportiva de primera categoría y con estándares internacionales.</p>

<p>“La modernización de la Unidad Deportiva Atanasio Girardot es un proyecto de infraestructura social deportiva que, por medio de una Asociación Público Privada, facilitará el crecimiento económico, turismo, comercial y cultural de la ciudad, fortaleciendo el tejido urbano y social de Medellín y el Área Metropolitana del Valle de Aburrá”, señaló el director de la Agencia APP, Rodrigo Foronda.</p>

<p>Esto contempla una inversión de más de 172 millones de dólares por parte de un inversionista privado y la remodelación de 283.600 metros cuadrados, de los cuales 129.000 pertenecen al estadio Atanasio Girardot, infraestructura en la que está enfocada la intervención central del proyecto. Contará con importantes reformas de diseño como la instalación de una cubierta para el escenario, la transformación de la fachada, la construcción de zonas comerciales, el mejoramiento de los sistemas de acceso, silletería nueva, un moderno sistema de drenaje y  tecnología de punta, que facilitará  la logística de los eventos deportivos y de entretenimiento.</p>

<p>Las reformas se realizarán con base en las necesidades actuales de la infraestructura deportiva, con las normas de accesibilidad universal, seguridad y evacuación. Además, se ajustan a los requerimientos de la Confederación Suramericana de Fútbol, Conmebol, para cumplir con certámenes sudamericanos.</p>

<p>Como complemento a la intervención del estadio, la Unidad Deportiva Atanasio Girardot contará con un hotel deportivo, en la actual Villa Deportiva Antonio Roldán, con una capacidad para 520 personas y con zonas de concentración, de hidratación-recuperación, gimnasio, consultorios médicos, entre otras.</p>

<p>A esta infraestructura se suma un edificio multipropósito, que estará al lado de la pista de atletismo y tendrá locales comerciales para el sector deportivo, gastronómico, farmacéutico y bancario, escenarios para disciplinas deportivas relacionadas con el mundo digital y una plaza para el disfrute cultural y familiar, junto a una oferta de parqueaderos para responder a la alta demanda del sector.</p>

<p>Finalmente, la modernización dará paso a la Zona Gastro, un espacio de dignificación del emprendimiento que acogerá a los venteros de la unidad deportiva, quienes estarán distribuidos en modernos y adecuados locales comerciales. Allí se les ofrecerá una infraestructura de calidad, oportunidades de inserción laboral, cualificación en competencias laborales y mayor diversidad comercial para mejorar sus ingresos. </p>

<p>Esta iniciativa aporta a la consolidación de una Ecociudad con un distrito deportivo renovado, amigable con el medio ambiente y adecuado para fortalecerse como un motor de desarrollo económico, turístico, deportivo y cultural para Medellín y Colombia.</p>

