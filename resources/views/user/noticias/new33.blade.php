<h4 text-align="center">
Cinco proyectos de la Alcaldía de Medellín fueron seleccionados para la Bienal Colombiana de Arquitectura y Urbanismo 

</h4>

                                <hr>

                                <ul>
                                    
<li>- La Alcaldía de Medellín participa en esta edición en las categorías: Diseño Urbano y Paisajismo y Ordenamiento Urbano Regional.</li>
    
<li>- Los ganadores se anunciarán entre el 17 y el 19 de noviembre de 2022.</li>

<li>- La 28 Bienal Colombiana de Arquitectura y Urbanismo es organizada desde 1962 por la Sociedad Colombiana de Arquitectos.</li>
    
    
    
    </ul>
    <hr>

<p>Los proyectos de Parque Prado, el Plan Integral de Gestión de Prado, el corredor de la Avenida Jardín, el Paseo Urbano de La Playa y el plan de gestión e intervención para el proyecto estratégico Centro Histórico de Medellín, fueron seleccionados para participar en los premios de la edición 28 de la Bienal Colombiana de Arquitectura y Urbanismo. Estas iniciativas tienen impacto en la sostenibilidad, la resiliencia urbana, la economía circular, el espacio público saludable y el mejoramiento del hábitat. </p>

<p>Dos de los proyectos seleccionados son gestionados por la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–. El primero es el Plan Integral de Gestión de Prado, en la categoría Ordenamiento Urbano y Regional, que incluye actividades como el diseño, la gestión y mejoramiento del espacio público en la carrera Palacé, nuevas zonas de estancia con mobiliario urbano y el embellecimiento de fachadas en la misma vía y alrededor del Parque Olano. El otro proyecto es el corredor verde de la Avenida Jardín que participa en la categoría Diseño Urbano y Paisajismo.</p>

<p>“Estamos felices porque dos de nuestros proyectos fueron seleccionados para esta bienal. Ambos están pensados para el disfrute y el encuentro de todos los ciudadanos; espacios públicos acogedores con más vegetación y más zonas para el peatón y para el esparcimiento”, indicó el subdirector de Gestión de Paisaje y Patrimonio de la Agencia APP, Daniel Madrigal Arango.</p>

<p>En la Avenida Jardín se intervinieron 1.294 metros cuadrados de espacio público, se realizaron siembras menores de 5.000 metros cuadrados y 370 nuevos árboles, y se instalaron 20 estancias lo que impacta la calidad de vida de cerca de 116.000 residentes.</p>

<p>“Agradecemos a la agencia APP con el proceso que se realizó en la zona para el aprovechamiento del espacio público, esto nos ayuda a que los transeúntes tengan mayor afluencia en el restaurante y se vea más concurrido y visitado por ciudadanos y turistas”, manifestó Yessid Canchila, administrador de restaurante en el sector de Avenida Jardín. </p>

<p>Además, el Parque Prado y el Paseo Urbano La Playa –Tramo 1– también fueron seleccionados en la misma categoría. La primera es una obra ejecutada por la Secretaría de Infraestructura con la generación de 5.450 metros cuadrados de espacio público, la siembra de 230 árboles y la creación de 2.800 metros cuadrados de zonas verdes.</p>

<p>La segunda fue realizada por la Empresa de Desarrollo Urbano –EDU–. “Esta es una obra diseñada por nuestro Taller de Diseño con la que propusimos la redistribución de 37.000 metros cuadrados del tramo 1 (entre el teatro Pablo Tobón Uribe y la Avenida Oriental) como un proyecto transformador de la Ecociudad. Además, es una intervención que se piensa también para los próximos 20 años, con lo que se haría la recuperación de la quebrada Santa Elena”, explicó el arquitecto líder, Julián Gómez, de la EDU.</p>

<p>Finalmente, la Gerencia del Centro, con el proyecto estratégico Centro Histórico de Medellín, se destaca por propiciar una alianza por el Territorio Histórico con el Metro, el Museo de Antioquia, Corpocentro y la Agencia APP para la planificación y gestión de recursos sociales, naturales y urbanísticos que ofrezcan a los residentes, a través de la resignificación de los usos existentes de este sector, la posibilidad de ocuparlo plenamente y aprovechar toda la potencialidad de su patrimonio, su espacio público y la oferta institucional. </p>

<p>“Estamos apoyando la iniciativa en la que trabaja el Metro desde hace varios años y, desde la Alcaldía, generamos una alianza para acompañarlos. Es ahí cuando surge esta propuesta a la que se han sumado los sectores público y privado, las entidades culturales, los comerciantes y la academia, todos para generar un proyecto en el que el diseño urbano, las propuestas arquitectónicas y la ocupación del espacio público se unen a las propuestas sociales, económicas, culturales y pedagógicas para la recuperación de nuestro territorio histórico”, dijo la gerente del Centro, Mónica Pabón.</p>

<p>La 28 Bienal Colombiana de Arquitectura y Urbanismo es un evento insigne que cumple 60 años de trayectoria y que selecciona las obras y trabajos más significativos que preservan y mejoran los valores urbanos, históricos, ambientales, sociales y culturales en Colombia. Los ganadores se anunciarán entre el jueves 17 y el sábado 19 de noviembre.</p>

