<h4 text-align="center">
Más de 1.500 empleos directos se recuperaron con la reactivación económica de Provenza, Astorga y Manila, en El Poblado
</h4>

                                <hr>

                                <ul>
                                    
<li>- Allí, se habilitaron 3.053 metros cuadrados para el uso y disfrute de la ciudadanía. </li>
    
<li>- Descuentos para los comerciantes del sector, por cerca de $309 millones, se aplicaron en la tarifa de Aprovechamiento Económico del Espacio Público (AEEP).</li>

<li>- Más de $1.000 millones se han recaudado en 2022 como fuente alternativa de recursos para la cualificación y sostenimiento del espacio público de Medellín a través de AEEP.</li>
    
    
    
    </ul>
    <hr>

<p>Gracias a las estrategias impulsadas desde la Administración Distrital e implementadas por la Secretaría de Gobierno, junto a la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP, la ciudad recuperó más de 1.500 empleos directos en el marco de la reactivación económica de los sectores de Provenza, Astorga y Manila, en el barrio El Poblado.</p>

<p>El director de la Agencia APP, Rodrigo Foronda Morales, manifestó que “ha sido un proceso muy exitoso en dos perspectivas: la primera, la reactivación económica para los empresarios del sector del entretenimiento de la ciudad; y la segunda, para consolidar allí, unos espacios de encuentro y disfrute. Gracias a la responsabilidad de los ciudadanos y al compromiso de los comerciantes se ha garantizado el éxito de este proyecto con el que se sigue consolidando la Medellín Futuro”.</p>

<p>Entre las acciones que dieron paso a estos resultados está la figura de Aprovechamiento Económico del Espacio Público (AEEP) y los corredores estratégicos, que promueven el turismo responsable en el sector. Esto ha permitido que la ciudadanía disfrute de más de 3.000 metros cuadrados al aire libre, los cuales fueron habilitados para generar más ofertas comerciales y espacios de inclusión, diversión, recreación y esparcimiento.</p>

<p>Lukas Peláez Holguín, comerciante del sector de Astorga, reconoció la labor de la Agencia APP “por brindarles este Aprovechamiento Económico del Espacio Público, con el que le han dado vida al Parque del Poblado con más de 25 locales comerciales y más de 2.000 empleos indirectos, y mostrando la oferta gastronómica y cultural que Medellín tiene en esta zona”.</p>

<p>Igualmente, gracias a discotecas, tiendas de moda, restaurantes, hoteles y hostales, licoreras, así como agencias de viaje y turismo, Astorga, Manila y Provenza tuvieron alrededor de 6.000 visitantes promedio en días festivos, y entre 19.000 y 22.000 visitantes promedio por semana. </p>

<p>El instrumento AEEP, además de generar un cambio en el paradigma de la ocupación con fines económicos del espacio público, ha superado los $1.000 millones de recaudo en 2022, recursos que serán reinvertidos para la cualificación y sostenimiento del espacio público de Medellín. </p>

<p>Esta es una herramienta de control y buenas prácticas, tanto para los visitantes como para los comerciantes involucrados, lo que genera conciencia colectiva por el respeto a los espacios de esparcimiento y encuentro; además, impulsa una movilidad activa y áreas de entretenimiento y comercio.</p>


