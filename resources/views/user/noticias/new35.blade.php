<h4 text-align="center">
Este año, la Administración Distrital ha sensibilizado a más de 146.000 personas sobre el cuidado del peatón en las vías 
</h4>

                                <hr>

                                <ul>
                                    
<li>- En agosto se adelantan operativos de control del buen uso de aceras, plazas y vías peatonales en diferentes puntos.   </li>
    
<li>- Este 17 de agosto es el Día Mundial del Peatón, fecha propuesta por la Organización Mundial de la Salud (OMS). En Medellín se estableció por medio del Acuerdo 43 de 2015.</li>

<li>- En lo corrido del año (corte al 12 de agosto), 63 peatones perdieron la vida en incidentes viales. Actualmente, la estrategia Pasos Seguros busca disminuir estas cifras.</li>
    
    
    
    </ul>
    <hr>

<p>Este miércoles, 17 de agosto, se realiza en Medellín la feria “Todos Somos Peatones”, que hace visible y exalta a quienes caminan por la ciudad. Hasta el 13 de agosto, 146.287 personas se han sensibilizado acerca del cuidado de la vida en la vía, 83.207 de ellas en calidad de peatones. </p>

<p>La Organización Mundial de la Salud (OMS) fijó esta misma fecha como el Día Mundial del Peatón, como una manera de recordar a Bridget Driscoll, primera persona atropellada por un automóvil en 1897. Medellín incorporó la conmemoración mediante el Acuerdo 43 del 2015, con miras a contribuir con la salud individual de quienes utilizan la caminata como transporte ocasional o frecuente.</p>

<p>En este día, la Administración Distrital invita a cuidar la vida con acciones sencillas en las calles: conducir a velocidades moderadas y evitar distracciones. Se resaltan, además, los beneficios de caminar, que conectan con la reactivación económica y el modelo de Ecociudad.</p>

<p>“En nuestro Plan de Desarrollo nos propusimos una meta de llegar a cinco muertes por cada 100.000 habitantes. Para Visión Cero, la única cifra permitida es cero. Trabajamos para recuperar espacios para los peatones y devolverles el centro. Nadie dijo que iba a ser fácil, pero estamos trabajando en ello y es una apuesta de ciudad”, indicó el secretario de Movilidad, Víctor Hugo Piedrahíta Robledo. </p>

<p>En 2021, 245 personas perdieron la vida en incidentes viales en la capital antioqueña, 97 de ellas fueron peatones (42 %). Entre el 1 de enero y el 12 de agosto de 2022, 152 personas murieron por esta misma problemática, 63 de ellas se movilizaban a pie.</p>

<p>A propósito de la fecha, las secretarías de Infraestructura y Movilidad firmaron la Carta Internacional del Caminar, herramienta que recoge las necesidades de los peatones y proporciona un marco común para ayudar a las autoridades a reorientar las políticas actuales para una cultura donde la gente elige caminar y reducir las condiciones que favorezcan un estilo de vida dependiente del automóvil, lo que representa más seguridad a la hora de caminar.</p>

<p>Este documento se suscribió en La Alhambra, sector histórico del Centro de Medellín, donde la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP– adelanta el mantenimiento y cualificación de fachadas de 34 Bienes de Interés Cultural (BIC) para mejorar el paisaje urbano y la seguridad, movilidad, legalidad y convivencia ciudadana.</p>

<p>“Recorrer la ciudad a pie no debe ser solamente una experiencia segura en movilidad, también debe ser paisajística y estética. Es por eso que, a raíz de la peatonalización de La Alhambra, la Agencia APP ha mejorado las fachadas de la mano de la comunidad y de los comerciantes para este importante sector. Esta es otra forma de construir la Medellín Futuro”, expresó el director de la Agencia APP, Rodrigo Foronda Morales.</p>

<p>La feria Todos Somos Peatones se desarrollará en diferentes sectores. Inicia en la Alhambra, donde habrá un acto simbólico con los agentes infantiles de la Secretaría de Movilidad, la Gerencia de Movilidad Humana y la Gerencia del Centro. Allí, hasta las 4:00 p. m., se llevarán a cabo jornadas pedagógicas para el desarrollo de conductas y hábitos de seguridad vial e integral para los peatones. Entre las 4:00 p. m. y las 8:00 p. m., la Gerencia del Centro contará con programación en la Plazuela de San Ignacio, que incluye tenis de mesa, ajedrez y estand de los gestores sociales en la Zona Urbana de Aire Protegido (ZUAP), para promover buenas prácticas de movilidad activa. </p>

<p>“Me parece muy importante la celebración del día del peatón, porque aprendemos estrategias de prevención para evitar incidentes en la vía para quienes día a día caminamos por diferentes calles de la ciudad, exponiendo nuestra vida”, afirmó Juan Carlos González, quien asistió a una de las sensibilizaciones.</p>

<p>Durante todo el mes se realizarán operativos para el control del buen uso de aceras, plazas, vías peatonales, separadores y franjas en sectores como la Universidad de Antioquia, Campo Valdés, Provenza, Astorga y Manila. Conducir un vehículo en estos espacios genera una multa equivalente a 30 salarios mínimos legales mensuales vigentes. </p>



