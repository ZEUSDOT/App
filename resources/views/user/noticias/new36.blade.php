<h4 text-align="center">
En 24 recorridos temáticos, los habitantes de Medellín conocerán historias y lugares patrimoniales 
</h4>

                                <hr>

                                <ul>
                                    
<li>- La comuna 10 - La Candelaria es la de mayor riqueza histórica, con más del 80 % del patrimonio declarado de la ciudad.  </li>
    
<li>- Los edificios Coltejer, Fabricato y Constain; el parque Berrío, la avenida Juan del Corral y el cerro El Picacho, entre otros, son algunos lugares que  redescubrirán los asistentes.  </li>

<li>- La programación y los requisitos para la inscripción se encuentran en <a href="https://app.gov.co/fiestadelpatrimonio2022">https://app.gov.co/fiestadelpatrimonio2022</a>.</li>
    
    
    
    </ul>
    <hr>

<p>En septiembre, Medellín celebra la cuarta edición de la Fiesta del Patrimonio “Historias por Contar” y este año la Administración Distrital, en articulación con colectivos ciudadanos, hará 24 recorridos temáticos gratuitos en los que los asistentes recogerán testimonios, harán memoria y redescubrirán lugares que siempre han hecho parte de la ciudad, pero que son desconocidos por sus habitantes.</p>

<p>La comuna 10-La Candelaria es el sector de la capital antioqueña que guarda la mayor riqueza histórica, pues más del 80 % del patrimonio declarado de Medellín se encuentra allí. Es por esta razón que el corazón de la ciudad será el epicentro de 14 de los recorridos, entre los que se destacan los organizados por la Gerencia del Centro, como el de “Patrimonio Urbanístico y Arquitectónico del Centro”, programado para el 10 de septiembre; “Ciudad Museo” que será el 14 de septiembre; la “Caminata Fotográfica”,  definida para el 20 y la “Maratón Retro de Fotografía”, que se realizará el 24. </p>

<p>“Con alrededor de 40 actividades, que se desarrollarán durante todo el mes, pondremos en valor el Patrimonio Cultural Material e Inmaterial de nuestra ciudad. Tendremos conversatorios, recorridos, eventos musicales y activaciones artísticas que serán una gran oportunidad para conocer, aprender y disfrutar de nuestra historia, de nuestra memoria y de nuestra cultura. Los esperamos. Están muy invitados”, indicó la gerente del Centro, Mónica Pabón.</p>

<p>La Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público-Privadas (Agencia APP) es otra de las entidades de la Administración Distrital que ha diseñado recorridos atractivos por lugares emblemáticos de Medellín, para conmemorar la Fiesta del Patrimonio. Entre los más destacados se encuentran el recorrido urbano por la arquitectura moderna y nuestro patrimonio, que se realizará el 23 de septiembre; los paseos por los bares y restaurantes más emblemáticos del Centro, programados para el 24 y el 30; y el recorrido nocturno por las historias, los mitos urbanos y los chismes del barrio Prado, que se llevará a cabo el 24.</p>

<p>El coordinador de los “Recorridos Temáticos Centro ConSentido”, Ómar Portela, precisó que “se contará con experiencias únicas como entrar a edificios que pocas veces están abiertos al público, además de catedrales, palacios y otras edificaciones patrimoniales. También, se accederá a puntos de estos lugares en los que hay murales que no están abiertos al público, para reconocer el gran valor artístico que tiene el Centro de Medellín y el resto de la ciudad, por fuera y dentro de sus edificaciones”. </p>

<p>La Secretaría de Medio Ambiente también programó varios recorridos guiados en los que se sensibilizará a los participantes sobre la historia, la riqueza y la memoria colectiva de Medellín que hay en sus árboles y palmas. Serán dos jornadas y se llevarán a cabo el sábado 17 de septiembre, en el parque del barrio Robledo, y el 24 por el corredor de La Playa y San Ignacio, en donde se encuentra un grupo importante de individuos arbóreos que hacen parte de la historia y de la memoria colectiva de la ciudad.</p>

<p>Medellín tiene 697 individuos arbóreos protegidos por el Decreto 598 del 5 de julio de 2019, el cual definió el manejo y protección para la preservación de los árboles y las palmas, patrimonio natural y cultural de la capital antioqueña.Medellín tiene 697 individuos arbóreos protegidos por el Decreto 598 del 5 de julio de 2019, el cual definió el manejo y protección para la preservación de los árboles y las palmas, patrimonio natural y cultural de la capital antioqueña.</p>

<p>“Para mí, el Mes del Patrimonio es muy importante, principalmente porque somos una ciudad que tiene cosas muy hermosas, tiene historia, tiene artistas que salieron de las entrañas de la ciudad y, normalmente, como jóvenes no tenemos la oportunidad de conocer estas cosas. Entonces, que tengamos este mes y que haya recorridos cada semana en diferentes lugares, nos da la oportunidad de conocer un poco más la ciudad y enamorarnos más de ella”, expresó María Fernanda Arboleda, quien participó en una de estas actividades.</p>

<p>Sandra López, quien también se sumó a las caminatas guiadas, señaló: “A mí me encantan los recorridos urbanos, porque conocemos muchas partes de la ciudad, especialmente su arquitectura, y también los grandes personajes que ayudaron a construir a Antioquia y a Colombia”.</p>

<p>Participar en los Recorridos Temáticos de la Fiesta del Patrimonio “Historias por Contar” no tiene costo y solo hay que ingresar al portal web: <a href="https://app.gov.co/fiestadelpatrimonio2022">https://app.gov.co/fiestadelpatrimonio2022</a>. Allí, se encuentra la programación de los eventos y actividades del mes. Cuando el interesado identifique los recorridos se puede inscribir a través de los correos y formularios que se indican en cada uno. Se recomienda que los asistentes usen ropa cómoda y que se hidraten constantemente. </p>

<p>Con estos recorridos y visitas guiadas, se invita a valorar, respetar y cuidar el legado arquitectónico y natural que el pasado les regala a los medellinenses, porque son referentes de memoria colectiva y parte innegable de la identidad cultural de la ciudad.</p>




