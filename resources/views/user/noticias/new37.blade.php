<h4 text-align="center">
La Alcaldía de Medellín intervendrá 60 esculturas en el marco de la Fiesta del Patrimonio 2022
</h4>

                                <hr>

                                <ul>
                                    
<li>- Estas obras brillarán de nuevo con un plan de mantenimiento, conservación y restauración. De ellas, 23 fueron creadas por el maestro Fernando Botero.  </li>
    
<li>- Son más de 60 actividades, todas gratuitas, para reconocer y apropiarse de las memorias de Medellín, bajo el lema Historias por Contar.  </li>

<li>- Más de veinte recorridos, mercados artesanales, charlas, conferencias, maratones fotográficas, tertulias y un concierto de cierre, el 30 de septiembre, serán parte de las actividades de esta fiesta. </li>
    
    
    
    </ul>
    <hr>
<p>Medellín celebra la Fiesta del Patrimonio con una serie de actividades de restauración, difusión y encuentros para residentes y turistas enmarcadas en esta celebración. Entre estas acciones, la Administración Distrital, a través de la fundación Ferrocarril de Antioquia, realizará el mantenimiento preventivo, limpieza y restauración de 60 esculturas, entre ellas 23 del maestro Fernando Botero, con una inversión de $490 millones. </p>

<p>Esta intervención se estructuró a partir de unos recorridos que permitieron seleccionar aquellas obras que requerían ser intervenidas con mayor prontitud. </p>

<p>“Desde la calle Junín celebramos la Fiesta del Patrimonio, en articulación con la Gerencia de Centro, la Agencia APP, el Centro Administrativo de Planeación, la Secretaría de Suministros y la Secretaría de Cultura Ciudadana. Nos unimos para celebrar el patrimonio; conversatorios, charlas, conversaciones e inversión económica en diferentes espacios de la ciudad con la intervención de 60 esculturas, incluyendo la escultura de la Familia Silletera y la Estatua de Bolívar”, destacó el secretario de Cultura Ciudadana, Álvaro Narváez Díaz.</p>

<p>Además, la comunidad disfrutará de más de 60 actividades organizadas por distintas dependencias de la Alcaldía de Medellín, que se suman a una oferta de organizaciones y colectivos locales, como evidencia de que el patrimonio no es algo institucional sino social y se construye conjuntamente.</p>

<p>La programación incluye el recorrido "Ruta de la Paz y el Patrimonio", que será el jueves 15 de septiembre y cuyo punto de encuentro será el Museo Casa de la Memoria, a las 4:00 p. m. </p>

<p>El jueves 22 de septiembre se realizará la instalación dispositivo artístico sobre las Memorias del barrio Prado Centro y Manrique, a las 5:30 p.m., en la Casa de la Cultura de Manrique. Además, habrá un recorrido artístico por la comuna 6 - Doce de Octubre para anunciar la socialización del proyecto: "Cultura y diversión a un solo clic. Cerro El Picacho, un lugar para disfrutar y cuidar". Esto será el miércoles 28 de septiembre, a las 5:00 p. m. </p>

<p>“Gracias a la administración y al alcalde Daniel Quintero por generar confianza en nuestra entidad, por creer en nosotros como empresa idónea para hacer la intervención sobre todos los bienes escultóricos, entre esos el mantenimiento a las esculturas de Fernando Botero, que hacen referencia histórica y estética”, afirmó el director de la Fundación Ferrocarril de Antioquia, Jhon Edwin García Cardona.</p>

<p>La Agencia APP ha organizado un amplio repertorio de actividades referidas a las memorias y los patrimonios. Uno de sus epicentros será la Casa Adrissa, una construcción declarada Bien de Interés Cultural, situada en el barrio Prado.  En esta zona habrá una sembratón de árboles, una estampatón, exposiciones y charlas, todas de libre acceso. Pero, además, esta entidad ha organizado una serie de rutas con énfasis en gastronomía, arquitectura y árboles.</p>

<p>“Nuestro patrimonio lo tenemos que cuidar, lo tenemos que poner en valor y procurar que los ciudadanos aprendan de este, lo vivan, lo quieran, se vuelquen sobre él, porque es nuestra historia viva, es la historia que tenemos”, dijo el director de APP, Rodrigo Foronda.</p>

<p>La programación concluye el 30 de septiembre con el concierto "Mujeres, huellas y raíces", a cargo de Explosión negra, agrupación de Medellín que resalta sus raíces africanas con el sabor, el color y la tradición negra. El espectáculo rendirá homenaje a dos lideresas del barrio Moravia: Mamá Chila y Heroína Córdoba. Esto será a las 5:30 p.m. en el auditorio del Centro Cultural Moravia. </p>

<p>Toda la programación del Mes del Patrimonio puede consultarse en el enlace: <a href="https://app.gov.co/fiestadelpatrimonio2022">https://app.gov.co/fiestadelpatrimonio2022</a>.</p>

<p>“Para nosotros es un honor que nos hayan tenido en cuenta en las actividades que la Secretaría de Cultura Ciudadana y la Alcaldía hacen este mes. El Astor cumplió 92 años y, como tal, hace parte del patrimonio de Medellín y es referente importante desde 1930. Los invitamos a que vuelvan a esos espacios de la ciudad”, agregó el gerente de la repostería Astor, Carlos Silva. </p>

<p>En septiembre, todos y todas pueden acercarse de otro modo a la historia local; una oportunidad para entender que el patrimonio dice mucho de lo que es Medellín actualmente, que permite cuestionar y construir entre todos el legado para las próximas generaciones. El PatrimonioMED es clave para construir la Medellín Futuro.</p>





