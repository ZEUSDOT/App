<h4 text-align="center">
La Alcaldía de Medellín intervendrá 60 esculturas en el marco de la Fiesta del Patrimonio 2022
</h4>

                                <hr>

                                <ul>
                                    
<li> Fue el resultado de un ejercicio de cocreación con ciudadanos, emprendedores y actores culturales del sector.  </li>
    
<li> La nueva imagen de Prado impulsará una estrategia para promover la cohesión social, los emprendimientos creativos y el turismo en la zona.   </li>

<li> Este lanzamiento y el concierto de aniversario del Aeropuerto Olaya Herrera cerraron oficialmente la cuarta edición de la Fiesta del Patrimonio 2022 - “Historias por Contar”, en la que participaron más de 3.000 personas.</li>
    
    
    
    </ul>
    <hr>
   
<p>Tras su declaración como territorio creativo por el Ministerio de Cultura, el barrio Prado, el único patrimonial de Medellín, avanza en su consolidación como centro de industrias creativas y culturales a través del lanzamiento de su imagen, la cual impulsará dinámicas de reconocimiento y apropiación por parte de los ciudadanos y fortalecerá valores e ideales comunes de residentes y propietarios. </p>

<p>La Alcaldía de Medellín, a través de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–, trabajó con la comunidad en espacios de cocreación para diseñar su imagen, la cual refleja atributos como patrimonio, arquitectura, cultura, memoria, historia, diversidad, creatividad y arte. Además, comunica a través de símbolos los deseos, los propósitos comunes y el objetivo de construir, de manera articulada y profunda, una visión del barrio en la que todos tengan un lugar y en la que se cuide, conserve y promulgue el patrimonio.  </p>

<p>“Este trabajo gráfico condensa el esfuerzo de la Administración Pública y de los privados para revitalizar nuestro barrio patrimonial. Todas las actividades recreativas, culturales y todas aquellas que representen una nueva cultura para nuestra ciudad están representadas aquí en el barrio Prado. Es por eso que la Agencia APP mantiene importantes esfuerzos por impulsar los espacios recreativos y culturales e incentivar el cuidado y la puesta en valor de nuestro patrimonio arquitectónico”, afirmó el director de la Agencia APP, Rodrigo Foronda Morales.  </p>

<p>La cocreación de la imagen de Prado es el primer paso de una estrategia territorial que se desarrollará e implementará en los próximos meses con las organizaciones, empresas y emprendedores, buscando posicionar al barrio como un polo de desarrollo de las industrias creativas y culturales en la ciudad y en otros países.</p>

<p>Para la profesora de arquitectura, Gilda Wolf, de la Universidad Nacional, quien es, además, especialista y magíster en Estética, y doctora en Gestión y Conservación del Patrimonio: “el origen de la palabra patrimonio viene de economía y es lo que se transmitía a la siguiente generación. A mí me gusta pensar en el patrimonio no como un pasivo, sino como un activo que se va a incrementar, lo cual pasa por un cambio de imaginario y en abrir las posibilidades de apropiación por parte de la población”.</p>

<p>El lanzamiento de la imagen de Prado cierra las actividades de la cuarta edición de la Fiesta del Patrimonio 2022 - “Historias por Contar”, que tuvo más de 60 actividades entre charlas, recorridos fotográficos, talleres, exposiciones, activaciones culturales, entre otras, y la participación de más de 3.000 personas. </p>

<p>Durante esta fiesta, distintas dependencias del Conglomerado Público impulsaron y organizaron eventos y actividades para que los asistentes aprendieran a valorar, respetar y cuidar el legado que les ha dejado el pasado a los medellinenses, especialmente en la comuna 10 (La Candelaria), la cual cuenta con más del 80 % del patrimonio declarado de la capital antioqueña. </p>

