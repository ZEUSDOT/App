<h4 text-align="center">
La comuna Manrique tendrá el macromural artístico más grande de Medellín 
</h4>

                                <hr>

                                <ul>
                                    
<li>Son más de 300 fachadas, medianeras y culatas que, gracias al trabajo articulado con la comunidad, se llenará de color bajo el nombre Constelaciones.</li>
    
<li> Además del macromural, otros 30 murales artísticos, entre los barrios Brisas del Jardín y San José de la Cima II, tienen diferentes apuestas estéticas que cuentan historias, vivencias y hacen homenaje a líderes sociales.   </li>

<li>Este nuevo trayecto de arte urbano se consolida como un destino turístico y cultural.</li>
    
    
    
    </ul>
    <hr>
   
<p>Un conjunto de estrellas es la vista que lucirá en unos meses el macromural Constelaciones en la comuna 3, Manrique, en los barrios Brisas del Jardín, San José de la Cima No. 2 y Balcones de Jardín. Son más de 44.500 metros cuadrados intervenidos en más de 300 fachadas, medianeras y culatas.</p>

<p>El proyecto avanza en tres fases y aportará al desarrollo integral de esta comuna, mejorando el paisaje, promoviendo la convivencia y entregando a Medellín un nuevo destino turístico y cultural para recorrer y disfrutar. </p>

<p>Este desarrollo urbano, conceptualizado y coordinado por el artista plástico Fredy Alzate, es ejecutado por la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP– con el apoyo de otros cuatro artistas y una inversión de $1.500 millones.</p>

<p>“Desde la Agencia APP de la Alcaldía de Medellín seguimos materializando el sueño de ver la ciudad llena de arte, magia y color. Es por eso que estamos interviniendo alrededor de 340 fachadas para hacer el macromural de arte urbano más grande que se tenga registro en la ciudad. Este es un trabajo de la mano de la comunidad, con cada uno de los propietarios de los predios, para entregarle a la comuna 3 una obra espectacular y seguir materializando la Medellín Futuro”, expresó el director general de la Agencia APP, Rodrigo Foronda Morales. </p>

<p>Constelaciones es el resultado de varias actividades como residencias artísticas para conocer en profundidad la zona, laboratorios creativos con los residentes, un reconocimiento activo del territorio, talleres de arte, activaciones artísticas, un diagnóstico social y la articulación entre Estado, ciudadanos y artistas. Por ello, también se han realizado diez activaciones como Feria de Emprendimiento, jornadas de cine y 31 talleres de sensibilización artística con 1.126 participantes.</p>

<p>“Hace cuatro meses llegó el proyecto de la Agencia APP de pintar el barrio y este es un proyecto maravilloso para nuestra comunidad. Es demasiado valioso para nosotros, importante, y estamos muy agradecidos”, afirmó Óscar Londoño, habitante del barrio y apoyo logístico.</p>

<p>Además del macromural, en la zona se realizó, con la participación de artistas urbanos y colectivos como Biógrafos, Jauría, Jagua, Lemm and Buda, Dubian Monsalve y Alejandro Agud, un trayecto con 30 murales y 1.100 metros cuadrados intervenidos que retratan escenas de los barrios impactados y se convierten en memoria viva de cotidianidades, líderes sociales y personajes del sector. </p>

<p>Para la líder y fundadora del barrio Brisas del Jardín,  María Janeth Alzate: “la gente está muy contenta con este proyecto. Todos están a la expectativa, mirando los murales y los jóvenes artistas que están pintando. Con muchas ansias por las cosas buenas y positivas que traerá este proyecto al barrio”.</p>

<p>La Agencia APP, en su propósito de cualificar y mejorar el paisaje, ha adelantado otras intervenciones en el puente de la 4 Sur, la Galería Urbana a Cielo Abierto de la Calle Barranquilla, el intercambio vial Punto Cero, entre otras, cumpliendo y superando la meta del Plan de Desarrollo de entregar 14.300 metros cuadrados de arte urbano a Medellín. </p>




