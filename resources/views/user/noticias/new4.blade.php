<h4 text-align="center">
La Alcaldía de Medellín gana otros 684 metros cuadrados de zonas verdes con la instalación de cuatro nuevos jardines verticales
</h4>

								<hr>


								<ul>
                                    
                                <li>De estos cuatro muros verdes, tres están dentro del Centro Administrativo Municipal y uno más en la parte externa del edificio del Concejo.
</li>

<li> La inversión es de $1.124 millones y en estos jardines verticales se siembran 34.884 plantas de diferentes especies. </li>

<li>Tienen la capacidad de producir oxígeno para 684 personas y absorben 1.466 kilogramos de partículas contaminantes cada año. </li>


</ul>
                                <hr>

								<p>
                                    
                                La instalación de estos cuatro jardines verticales continúa consolidando a Medellín como Ecociudad. Tres estarán en el primer piso del edificio de la Administración Municipal, al interior de las instalaciones, y uno más en la parte externa del edificio del Concejo.                                 </p>
							
<p>

El proyecto, liderado por la Agencia APP y financiado por la Secretaría de Infraestructura Física, tiene una inversión de $1.124 millones y en él se siembran 34.884 plantas de diferentes especies.
<p>
De esta manera, Medellín gana 684 metros cuadrados de nuevas zonas verdes. Estas acciones no solo mejoran la calidad del aire, sino que embellecen los espacios. Los jardines verticales responden a las propuestas consignadas en el Plan de Desarrollo Medellín Futuro 2020–2023 y promueven acciones de adaptación al cambio climático. 
<p>
“Con esta iniciativa cualificamos el espacio público, mejoramos la conectividad biológica y adecuamos lugares para enriquecer y consolidar el sistema ambiental de la ciudad con infraestructura verde alternativa”, explicó la secretaria de Infraestructura Física, Natalia Urrego Arias.
<p>
Los jardines verticales instalados en diferentes puntos del Centro Administrativo La Alpujarra aportan a la reducción de la temperatura y se convierten en refugio para diferentes especies de fauna. Además, tienen la capacidad de producir oxígeno para 684 personas y absorben 1.466 kilogramos de partículas contaminantes cada año.

<p>
“Hacer de Medellín una Ecociudad es uno de los pilares fundamentales de nuestra ruta de gobierno. Desde la Agencia APP brindamos toda nuestra experiencia y conocimiento para mejorar el paisaje urbano, contribuir al cuidado del medio ambiente y brindar espacios coloridos, sostenibles y de alto impacto visual y arquitectónico para el disfrute de la ciudadanía”, agregó el director general de la Agencia APP, Rodrigo Foronda Morales. 
<p>

Cuatro puentes peatonales ubicados en los barrios Carlos E. Restrepo, Caribe, Conquistadores y Guayabal también tendrán jardines verticales.
