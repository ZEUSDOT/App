<h4 text-align="center">
El alcalde Daniel Quintero Calle oficializó el decreto que habilita las Áreas de Revitalización Económica (ARE) para potenciar el uso del espacio público
</h4>

                                <hr>

                                <ul>
                                    
<li>Este instrumento articula a la Administración y a los privados para crear, mantener y consolidar zonas estratégicas para el desarrollo comercial. </li>
    
<li> La primera ARE está ubicada en Provenza, Manila y el Parque Lleras, donde se generan 3.800 empleos directos.   </li>

<li>Gracias a iniciativas de reactivación económica como estas, Provenza fue considerada por la Revista Time Out como la calle más “cool” del mundo.</li>
    
    
    
    </ul>
    <hr>
   
    <p>El alcalde Daniel Quintero Calle firmó y oficializó el Decreto 667 del 2022, el cual habilita la conformación de las Áreas de Revitalización Económica (ARE) en Medellín, iniciando por la ubicada en Provenza, Manila y el Parque Lleras, donde actualmente se generan 3.800 empleos directos, con el objetivo de coadministrar y cuidar el espacio público, crear empleo, dinamizar el comercio y promover espacios seguros alrededor del disfrute y del entretenimiento en zonas estratégicas de Medellín.</p>

    <p>Las ARE son espacios públicos que serán entregados por la Administración Distrital a entidades públicas o particulares para promover la asociación y la gestión del espacio público con beneficios como la solución de problemáticas alrededor de los negocios, la administración eficaz de los espacios, la promoción de la sostenibilidad ambiental, social y económica, el mejoramiento de la infraestructura física y una mayor oferta de actividades culturales, gastronómicas y de entretenimiento. </p>

    <p>“Esto va a permitir poner al ciudadano en el centro al unir los esfuerzos, no solo de la administración sino también de los comerciantes, para enfrentar temas de seguridad, basuras, movilidad, ruido y, en general, el embellecimiento de la zona. Esta zona (Provenza), junto a Manila y el Parque Lleras han creado todo un espíritu de trabajo conjunto que ha desatado una nueva forma de ver lo público y lo privado trabajando de forma coordinada”, afirmó el alcalde.</p>

    <p>Las ARE potenciarán los resultados del instrumento de Aprovechamiento Económico del Espacio Público, el cual ha logrado que Provenza, donde confluyen 247 comerciantes, haya sido denominada una de las 33 calles más “cool” del mundo, según la Revista Time Out. </p>
    
    <p>Asimismo, ha ayudado a recuperar 1.100 empleos en Provenza, Manila, Astorga y el Parque Lleras, recaudar más de $918 millones para reinvertir en el sostenimiento del sistema de espacio público de Medellín, posibilitar la reapertura de 43 establecimientos comerciales y promover la visita de 6.000 personas en promedio en días festivos, y entre 19.000 y 22.000 visitantes promedio por semana. </p>

    <p>“Este es un ejercicio de confianza recíproca entre los privados y la administración. Los beneficios de las ARE son incontables: corresponsabilizar a los privados del manejo de residuos, del manejo del volumen de los establecimientos comerciales, de la seguridad y de la realización de eventos. Ahí vamos a estar juntos, públicos y privados, en esta iniciativa de revitalización económica. Empezamos por este, pero vendrán muchos más para que la ciudadanía se apropie de los espacios públicos”, explicó el director general de la Agencia APP, Rodrigo Foronda Morales. </p>

    <p>Los ARE se conforman de asociaciones de personas naturales, comunidades organizadas, asociaciones cívicas, gremiales y comerciales, por medio de una persona jurídica sin ánimo de lucro. Asimismo, se financian con aportes voluntarios, por Aprovechamiento Económico del Espacio Público (AEEP), recursos públicos, entre otros.</p>

    <p>“Esto se ha podido dar gracias a la colaboración en conjunto, hemos estado unidos y eso hizo posible lo que hoy es Provenza, y la idea es que sea un sitio sano para la gente, la familia y el entretenimiento. Yo quiero agradecerles porque después de la pandemia tuvimos esa ayuda que hoy nos permite ser una de las calles más cool del mundo”, dijo la directora de la Corporación Barrio Provenza, Juanita Cobollo.</p>

    <p>En el mundo, los ARE también son denominados Centros Comerciales a Cielo Abierto (Argentina) o Distritos de Mejoramiento Comercial - B&D (Canadá). </p>




