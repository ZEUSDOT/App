<h4 text-align="center">
La Alhambra se renueva con la intervención de 34 fachadas y más espacio público para la temporada de compras navideñas
</h4>

                                <hr>

                                <ul>
                                    
<li>Esto fue posible gracias a una alianza público privada con aporte de recursos económicos del 80 % de la Administración Distrital y del 20 % de los comerciantes del sector.</li>
    
<li>En La Alhambra también se mejoraron jardineras, se instaló mobiliario urbano y se reguló el uso de circulación de vehículos. </li>

    
    
    
    </ul>
    <hr>
   
    <p>En la tradicional calle del Centro Histórico de Medellín, La Alhambra, 34 fachadas de Bienes de Interés Cultural fueron mejoradas para destacar sus cualidades arquitectónicas y renovar el paisaje urbano de esta calle comercial. La intervención fue posible gracias a una alianza público privada entre la Administración Distrital, a través de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas -Agencia APP- y los comerciantes del sector organizados en agremiaciones como Asoguayaquil. </p>

    <p>“La Agencia APP entrega hoy la renovación de la calle La Alhambra. Son 34 fachadas que hemos puesto en valor. De la mano de los privados y con la contribución de las secretarías de Infraestructura Física y Movilidad, la ciudad cuenta con un nuevo pulmón ambiental y comercial. La percepción de seguridad, la movilidad y el incremento de las ventas demuestran cómo, trabajando juntos, logramos frutos considerables para beneficio de todos”, indicó el director general de la Agencia APP, Rodrigo Foronda Morales. </p>

    <p>La Alhambra hace parte del denominado Conjunto Guayaquil, el cual cuenta con tres Bienes de Interés Cultural del Ámbito Nacional como el Edificio Carré, el Edificio Vásquez y la Estación Medellín – Cisneros, y alrededor de otros 48 Bienes de Interés Cultural del ámbito municipal, declarados y protegidos mediante el Plan de Ordenamiento Territorial.</p>

    <p>Para esta transformación se realizó un análisis de las escalas estratigráficas en cada una de las fachadas y se recuperó la paleta de colores pasteles y coloridos para consolidar un ambiente diverso y armónico. </p>

    <p>Asimismo, se desarrollaron actividades de mantenimiento de techo en teja de barro, lavado de fachadas, pintura y resane de muros y cielos, traslado de aires acondicionados y antenas a zonas internas de los locales, retiro de elementos como carpas, ganchos, letreros, publicidad, entre otros, instalación de avisos comerciales y parasoles unificados en materialidad y color, y pintura de puertas y ventanas.</p>

    <p>“El arreglo de fachadas nos ha beneficiado mucho, porque nos ha aumentado el flujo de personas al sector, ha embellecido la cuadra y nos ha permitido mejorar las dinámicas con el aprovechamiento económico del espacio público para hacer de esta zona un lugar en el que la gente venga tranquilamente al Centro”, puntualizó Andrés Aristizabal, comerciante del sector por más de 20 años.</p>

    <p>Con otras acciones como el mejoramiento de andenes y jardineras, el aprovechamiento del espacio público para uso comercial, y la regulación de los horarios de cargue y descargue, La Alhambra se consolida como un modelo de organización, convivencia y renovación del espacio público para el disfrute de turistas y visitantes en el Centro de Medellín. </p>

    <p>Para el director de Asoguayaquil, Daniel Manzano Cárdenas, “La Alhambra, un sector icónico de la ciudad, es símbolo y modelo a replicar. Hoy esta vía es lo que en otro tiempo fue para que la gente la visite, vea una amplia propuesta comercial y se sienta segura”.</p>

    <p>La Alhambra también tiene una peatonalización temporal, entre las calles 45 y 46, que opera entre las 10:00 a. m. y las 7:00 p. m., como una estrategia de fortalecimiento y promoción de movilidad sostenible, desarrollada por la Secretaría de Movilidad en esta Zona Urbana de Aire Protegido.</p>

    <p>Esta iniciativa aporta a la disminución de emisiones de CO2 y PM2.5 (material particulado), con mejoras a las condiciones de movilidad activa y como garantía de apropiación del espacio público por parte del peatón, trabajadores del sector y comunidad en general. Igualmente, se consolida como un punto de encuentro y se mitigan las malas prácticas de movilidad en la zona frente al mal parqueo.</p>

    <p>“Esto nos tiene muy contentos porque seguimos dándole coherencia al debate de la movilidad, primero el peatón, segundo la movilidad activa y tercero el transporte público, la razón de ser de la Secretaría de Movilidad en la construcción de la Medellín Futuro”, indicó el secretario de Movilidad, Víctor Hugo Piedrahíta.</p>

    <p> Entre otros beneficios de esta renovación urbana en La Alhambra se encuentran el mejoramiento de la percepción de seguridad, la reducción del ruido, la activación de actividades culturales y comerciales, el incremento de visitantes y ventas, un adecuado manejo de residuos y la entrega de un corredor comercial renovado para la temporada de compras navideñas con más de 50 locales comerciales impactados.</p>

