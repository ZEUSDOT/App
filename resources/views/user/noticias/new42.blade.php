<h4 text-align="center">
771 habitantes de bienes patrimoniales del barrio Prado se beneficiarán con la reducción en las tarifas de servicios públicos
</h4>

                                <hr>

                                <ul>
                                    
<li>Entre $800.000 y casi $2.000.000 será el ahorro anual proyectado para cada vivienda.</li>
    
<li>Esto es posible gracias al instrumento de asimilación de estratificación, en el que se calcula la tarifa de servicios públicos con un estrato más bajo. </li>

<li> El descuento es automático y se reflejará desde noviembre en la factura de servicios públicos.</li>

    
    
    
    </ul>
    <hr>
   
    <p>771 Bienes de Interés Cultural (BIC) de uso residencial de Prado, barrio patrimonial de Medellín, se beneficiarán gracias a la asimilación por estratificación, que no significa el cambio de estrato pero que sí permitirá que se calculen las tarifas de servicios públicos con base en uno más bajo, según lo reglamenta el Decreto Municipal 0227 de 2022.</p>

    <p>El descuento es automático y se reflejará desde noviembre en la factura de servicios públicos. Esto permitirá que los habitantes ahorren entre $800.000 y casi $2.000.000 cada año por vivienda, dependiendo del consumo.</p>

    <p>Este beneficio, fruto de la gestión y articulación entre la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas -Agencia APP y el Departamento Administrativo de Planeación -DAP, se entiende como una compensación económica por las limitaciones de construcción que implica tener una vivienda patrimonial. </p>

    <p>“De lo que se trata es de una reducción en la tarifa de los servicios públicos. Es decir, a las personas que viven en estratos 4 o 5, se les cobra como si estuvieran en estratos 1 o 2 dependiendo de la ubicación del bien. Así estamos compensando en algo las imposibilidades de intervención de los BIC con la liberación de unos recursos. Lo que buscamos es que las personas sigan preservando estos bienes y que sigan siendo casas de habitación para mantener la esencia del barrio”, indicó el director general de la Agencia APP, Rodrigo Foronda Morales. </p>

    <p>Los recursos son financiados por el Distrito y el Fondo de Solidaridad de la Comisión de Regulación de Energía y Gas (CREG), y ascienden a un promedio anual de $661 millones.</p>

    <p>Édgar Antonio Ruiz, habitante de Prado hace 33 años, dijo: “Esta compensación la merecemos porque, como propietarios de estas casas, no podemos hacerles mucho. Con este beneficio logramos ahorrar o invertir en el mantenimiento de las viviendas”. </p>

    <p>Esta asimilación no afecta el valor comercial de la propiedad. Asimismo, es permanente y solo se puede perder por un cambio de uso del BIC, de residencial a comercial u otros.</p>

    <p>“Estamos muy contentos con la aprobación del descuento en los servicios públicos. Es algo que nos va a beneficiar mucho a los residentes del sector porque así podemos hacer mayor mantenimiento a las casas y le servirá mucho al barrio para que se vea en mejor estado para turistas y visitantes”, indicó Jairo Correa, habitante de Prado.</p>

    <p>Con esta estrategia, se busca incentivar el cuidado del patrimonio y acompañar a los propietarios y residentes de Prado en el mantenimiento de los Bienes de Interés Cultural, así como generar un auxilio económico con libre destinación para contribuir al mejoramiento de la calidad de vida de las familias.</p>

