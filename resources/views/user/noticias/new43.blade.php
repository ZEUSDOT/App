<h4 text-align="center">
Más de 150 venteros conocieron avances y logros en la modernización de la Unidad Deportiva Atanasio Girardot 
</h4>

                                <hr>

                                <ul>
                                    
<li> En el evento se presentaron los alcances técnicos y las oportunidades para mejorar las ventas y negocios de los emprendedores.</li>
    
<li>Este espacio ratifica el compromiso en mantener un diálogo constructivo y colectivo para desarrollar este proyecto estratégico.</li>

<li>Así cierra el ciclo de mesas técnicas que se realizaron durante el segundo semestre de 2022. </li>

    
    
    
    </ul>
    <hr>
   <p>Como una oportunidad para mejorar las ventas de comidas y productos, contar con espacios más óptimos para recibir a visitantes y turistas, y estar cerca de zonas con mayor circulación y de múltiples escenarios deportivos, parqueaderos y ciclorrutas, ante más de 150 comerciantes fue presentado el proyecto de modernización de la Unidad Deportiva Atanasio Girardot.</p>

   <p>Gracias a este espacio, que cierra el ciclo de seis mesas técnicas que se realizaron durante el segundo semestre de 2022 con la participación de seis líderes de los venteros y bajo el liderazgo de la Secretaría de Gobierno y la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas -Agencia APP-, se resolvieron dudas, mitos y expectativas que genera este proyecto. </p>

   <p>“La modernización de la Unidad Deportiva Atanasio Girardot es una realidad. Nos hemos reunido con cerca de 150 venteros informales de alrededor del estadio. Con ellos, y con la oferta institucional de la Administración Distrital, les hemos ofrecido a los emprendedores muchas alternativas para dar ese paso de la informalidad a la formalidad y que va a requerir que ellos hagan parte de la concesión de la modernización y la operación de la unidad deportiva. Están felices y nos han manifestado que ven el proyecto favorable y que es un salto grande para mejorar sus condiciones económicas y familiares”, explicó el director general de la Agencia APP, Rodrigo Foronda Morales. </p>

   <p>Se trataron temas como las obligaciones causadas por los servicios públicos, la vinculación entre los comerciantes y el futuro concesionario, la explicación de las intervenciones proyectadas, las dimensiones espaciales de los locales y las principales recomendaciones para hacer de esta iniciativa una oportunidad para participar de planes de formación comercial y empresarial, ofrecer productos y servicios de alta valoración por parte de los ciudadanos, mejorar la competitividad y, por ende, la calidad de vida. </p>

   <p>La modernización de la Unidad Deportiva comprende un estadio más seguro, cómodo, accesible y conectado, con un aforo de más de 48.000 espectadores, una nueva fachada, cubierta, rampas, palcos, museo, comercio, servicios y un espacio público renovado y accesible para todos.</p>

   <p>El nuevo edificio multipropósito, por su parte, contará con servicios complementarios para el deporte: locales comerciales, oficinas, baños públicos, juegos infantiles y parqueaderos. Además, los emprendedores que han permanecido por años en los alrededores de la unidad deportiva, tendrán más oportunidades a través de la formalización y cualificación en competencias laborales para mujeres, jóvenes y adultos mayores.</p>

   <p>Igualmente, con la nueva zona del emprendedor, se proyectan más locales comerciales modernos y adecuados, y un hotel deportivo con más de 500 camas, zonas de recuperación y de coworking, salones de eventos, oficinas comerciales y otros servicios que contribuyan a la salud física y mental de los deportistas.</p>

   <p>“El balance es muy positivo para nosotros. La mesa técnica en la que nosotros participamos con la Agencia APP y las diferentes secretarías de despacho fue muy positiva. Avanzamos en unos puntos y, lo más importante que tengo para resaltar, es que fuimos escuchados. Y, al hacerlo, pudimos plantear nuestras inquietudes y preocupaciones, así como hacer nuestras propuestas”, expresó el comerciante e integrante de la Red de Venteros del Estadio, Alexander Ramírez.</p>

   <p>La zona del emprendedor potenciará las dinámicas comerciales y turísticas de la unidad deportiva y estará ubicada en un punto obligado de circulación. Asimismo, contará con mesas, zonas técnicas, circulación amplia, accesibilidad inclusiva y una oferta diversificada de calidad.</p>

   <p>Las mesas técnicas evidencian el compromiso de la Administración Distrital en lograr un proyecto que comprenda las necesidades de la comunidad, los emprendedores, los turistas, los deportistas y las personas que disfrutan de este ícono de ciudad.</p>

   <p>Con la modernización de la Unidad Deportiva Atanasio Girardot se consolidará a Medellín como un escenario de convivencia, competitividad y desarrollo económico, y posicionará a la capital antioqueña como un destino para la inversión y la cooperación. </p>


