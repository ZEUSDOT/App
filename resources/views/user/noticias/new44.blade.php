<h4 text-align="center">
Los edificios Palacé, Constain y Cárdenas renuevan el paisaje urbano del Centro Histórico de Medellín
</h4>

                                <hr>

                                <ul>
                                    
<li>Con una inversión superior a los $1.000 millones, se adelantaron actividades de embellecimiento de fachadas y medianeros de tres Bienes de Interés Cultural.</li>
    
<li>Durante 2022, la Agencia APP gestionó el patrimonio de Medellín con actividades de mejoramiento de fachadas en Prado, La Alhambra y el Centro Histórico.</li>

<li>Junto a la iluminación arquitectónica de La Naviera, esta renovación consolida un nuevo eje atractivo turístico y patrimonial. </li>

    
    
    
    </ul>
    <hr>
   
<p>Con el propósito de mejorar el paisaje urbano, cualificar el patrimonio arquitectónico y mejorar el aspecto físico de las zonas intervenidas, la Alcaldía de Medellín, a través de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas -Agencia APP-, adelantó actividades de limpieza, pintura, instalación de parasoles, unificación de avisos en estilo y materiales, reparación de carpinterías metálicas y de madera, adecuación de redes externas y direccionamiento de aguas lluvias para evitar humedades futuras en los edificios Palacé, Constain y Cárdenas. </p>

<p>“La Agencia APP le entrega a Medellín la puesta en valor de los edificios Cárdenas, Palacé y Constain. Gracias a una inversión de más de $1.000 millones intervinimos más de 12.000 metros cuadrados. Este es un trabajo realizado de la mano de los privados que, alrededor de la Plazuela Nutibara, acompañan esta labor integral para embellecer el Centro de la ciudad y seguir construyendo la Medellín Futuro”, afirmó el director de la Agencia APP, Rodrigo Foronda Morales.  </p>

<p>Estas obras fueron posibles gracias a una alianza público-privada entre comerciantes y la Agencia APP, en la que los primeros aportaron el 20 % de los recursos y la entidad distrital el 80 % restante. En total, fueron intervenidos 6.200 metros cuadrados de los edificios Palacé y Constain, y 6.200 metros cuadrados en Cárdenas. </p>

<p>Los tres, ubicados en el Centro Histórico de Medellín, representan el desarrollo de las primeras décadas del siglo veinte y se constituyen, con la renovación de sus fachadas, en un nuevo eje turístico, cultural y patrimonial para el disfrute de locales y turistas.</p>

<p>“Un especial agradecimiento a la Alcaldía de Medellín por las intervenciones realizadas en los edificios y por el embellecimiento arquitectónico urbano. Esto nos ha ayudado mucho con el aumento de las ventas en el sector”, dijo James Conde, comerciante del edificio Palacé. </p>

<p>La construcción del Constain y Palacé fue promovida por Don Alberto Constain y realizada en tres etapas: la inicial del primero de ellos, se adelantó entre 1915 y 1918 con diseño del arquitecto Enrique Olarte; la segunda etapa corresponde al edificio Palacé y, la tercera, a la estructura que da a la avenida Primero de Mayo. </p>

<p>El edificio Cárdenas, por su parte, fue diseñado por el arquitecto Roberto Vélez, en 1941. El inmueble tiene un zócalo totalmente comercial y siete pisos de altura, más una terraza. Su fachada curva da continuidad con el edificio contiguo, el Álvarez Santamaría. Este tipo de edificación es un documento histórico que representa una etapa del desarrollo social y cultural de la ciudad y, por lo tanto, de la evolución del urbanismo, la arquitectura y la reinterpretación de conceptos modernos. Su construcción se adelantó entre 1944 y 1947. </p>

<p>Además de estas intervenciones, la Agencia APP adelantó, en 2022, otras acciones encaminadas a la gestión y cualificación del patrimonio. Entre ellas, la de más de 80 fachadas y más de 3.500 metros cuadrados en la carrera Venezuela y la calle Jorge Robledo, en el barrio patrimonial Prado, con una inversión superior a los $1.389 millones. </p>

<p>“La intervención me gustó mucho por la iluminación que tienen los letreros de los locales. Los negocios son más visibles. Van a llegar más clientes y la seguridad va a estar mucho mejor en la zona”, señaló Cindy Palacios, comerciante del edificio Cárdenas.</p>

<p>También, se destaca la renovación de fachadas en la calle histórica y comercial La Alhambra, en donde 34 fachadas de Bienes de Interés Cultural fueron mejoradas para resaltar sus cualidades arquitectónicas. La intervención fue posible gracias a una alianza público-privada entre la Administración Distrital, a través de la Agencia APP, y los comerciantes del sector organizados en agremiaciones como Asoguayaquil.</p>

<p>Asimismo, se avanza en la iluminación arquitectónica de diferentes inmuebles como el Centro Administrativo Distrital, el edificio Coltejer, la iglesia del Perpetuo Socorro y el hotel Nutibara. </p>

<p>Con estas obras, la Agencia APP apuesta a destacar y cualificar la arquitectura de los edificios icónicos del desarrollo de la ciudad, mejorar el paisaje urbano para el disfrute de todos y entregarle a la ciudadanía una Medellín renovada, que conserva su pasado y construye su futuro.</p>



