          <h2>    En Medellín se instalará obra ganadora de Concéntrico, uno de los eventos de arte y arquitectura más importantes en el mundo</h2>
        </div>

        <p>Medellín estará ante los ojos del mundo con la instalación temporal de una obra de arte en la Plaza de las Luces, como parte del desarrollo del noveno Festival Internacional de Arquitectura y Diseño de Logroño, uno de los más importantes de Europa y del mundo. </p>

        <p>“La Agencia APP, de la mano de Concéntrico, ha seleccionado la obra “Pul” para hermanar a ambas ciudades en la cualificación del espacio público. Veremos entre los edificios Carré y Vásquez, en el Parque de las Luces, esta obra que tiene un llamado a la interacción con la ciudadanía y que tiene, además, un enriquecimiento arquitectónico y paisajístico alrededor de estos edificios patrimoniales tan importantes para Medellín. Esta es una forma de convocar y de ponernos en el concierto internacional de la arquitectura y del espacio urbano”, explicó el director general de la Agencia APP, Rodrigo Foronda Morales.</p>

        <p>“Pul” es una propuesta de Meii estudio (Elvira Carrión y José Mateo) y los profesionales Clara Torregrosa y Gabriel Millán, de España. Esta obra, junto a la reciente renovación del paisaje de La Alhambra y los edificios patrimoniales de Vásquez y Carré, consolidarán al corazón administrativo de Medellín como un punto de visita, encuentro, diálogo y disfrute entre ciudadanos.</p>

        <p>A través de una convocatoria abierta, que cerró el pasado 10 de enero, se presentaron 484 proyectos de 55 países que reflexionaron sobre lo público, su apropiación, lo urbano y las ciudades creativas. </p>

        <p>De todas las postulaciones y una vez estudiados los proyectos y realizadas las oportunas deliberaciones, un jurado de la organización preseleccionó las mejores propuestas y un comité evaluador de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas -Agencia APP- escogió la propuesta para Medellín.</p>

        <p>El festival Concéntrico, que se realizará en España entre el 27 de abril y el 2 de mayo, invita a recorrer la ciudad mediante instalaciones, exposiciones, encuentros y actividades que proponen nuevos usos colectivos fortaleciendo la idea de comunidad en el espacio público. </p>

        <p>En Medellín, el festival tendrá la participación con la instalación de la obra “Pul”, la cual también será expuesta en Logroño (España) y que apuesta por presentar la comodidad del hogar en el espacio público exterior, diluyendo los límites entre la vivienda privada y el espacio colectivo, convirtiendo los cuidados y el confort en algo compartido. Asimismo, la instalación temporal es un espacio abierto, libre y gratuito en el que no se marca un uso o código determinado. </p>

        <p>Este evento ha reunido en sus ocho ediciones anteriores más de 100 intervenciones urbanas creadas por equipos de arquitectos y diseñadores que experimentan nuevos campos del diseño del entorno. </p>
        
