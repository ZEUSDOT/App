          <h2> Medellín construirá la Cárcel Metropolitana para sindicados con la aprobación de vigencias futuras en el Concejo Distrital</h2>
        </div>

        <ul>
            <li>El proyecto fue aprobado por el Concejo Distrital y permitirá  albergar a 1.339 personas privadas de la libertad en seis pabellones. </li>
            <li>Después de definir los diseños, la construcción empezará en 2024. El proyecto se terminará en 2026, año en el que comenzará a funcionar.</li>
            <li>En total se aprobaron vigencias futuras por más de $56.200 millones anuales por 12 años para la ejecución del proyecto. </li>
        </ul>

        <p>Con Once votos positivos y 9 negativos, el Concejo de Medellín, aprobó en sesión plenaria el presupuesto en vigencias futuras para la construcción de la Cárcel Metropolitana para sindicados, que estará ubicada en el corregimiento de San Cristóbal y que cuando comience a operar, tendrá capacidad para albergar a 1.339 personas en calidad de sindicados. Ello permitirá, en el corto plazo, el deshacinamiento de las estaciones de policía de la ciudad, que en promedio es de más del 200 %. </p>

        <p>“Presentamos el proyecto de construcción de la Cárcel Metropolitana para Sindicados, que es de suma importancia para dignificar las condiciones en las que se encuentran los privados de la libertad en las 27 estaciones de la Policía del Distrito de Medellín que nos genera una cantidad de problemas, entre ellos de seguridad, porque tenemos a los policías  en funciones que no les corresponden. Son 108 policías dedicados a la custodia y que deberían estar en las calles generando condiciones de seguridad para los ciudadanos”, dijo el Secretario de Seguridad y Convivencia, BG. José Gerardo Acevedo Ossa.  </p>

        <p>Con esta iniciativa se cumple con los fallos de la Corte Suprema de Justicia y la Corte Constitucional, que obliga a los epicentros metropolitanos a tener un reclusorio de estas características para cumplir con la ley vigente. Cabe agregar que este mandato había sido aplazado por todas las administraciones anteriores y en consecuencia, tras los estudios de viabilidad y diseño necesarios, en este caso se pudo sacar adelante con un modelo que esta vez integra a la Agencia Público Privada.</p>

        <p>“Este proyecto que presentamos ante el Concejo Distrital es de una importancia inusitada en este momento. Resuelve dos problemas flagrantes de la comunidad. El primero es el hacinamiento en las Estaciones de Policía, inhumano, indigno, falto de salubridad, toda una problemática que alcanza las 2.000 personas aproximadamente. En segundo lugar el cumplimiento de una sentencia judicial de la Corte Suprema de Justicia, que obliga a los municipios a resolver este problema, que es una bomba de tiempo para la ciudad”, dijo Rodrigo Foronda Morales, director de la Agencia Público Privada (APP). </p>

        <p>La Cárcel Metropolitana para Sindicados es el primer proyecto de Asociación Público Privada de iniciativa pública en Colombia con vigencias futuras aprobadas. Esta iniciativa fue estructurada por la Agencia APP con la misión de garantizar la dignidad y la prevalencia de los derechos humanos de las personas en condición de sindicadas. Este proyecto busca vincular mediante un contrato de concesión a los privados para la construcción, operación, mantenimiento y reversión de la infraestructura.</p>

        <p>En total se aprobaron vigencias por $56.210.140.000 anuales efectivos entre 2026 y 2037. La construcción se prevé comience en 2024 y su funcionamiento en 2026 bajo el modelo público privado, poniendo a Medellín como líder en esta iniciativa en el país y cumpliendo con la norma a cabalidad. </p>


