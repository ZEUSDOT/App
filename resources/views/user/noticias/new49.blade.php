          <h2>Ciudadanos y turistas en Medellín ya disfrutan del abrazo del Parque Lleras</h2>
        </div>

        <ul>
            <li>Con la intervención, que le dio prioridad al peatón, se renovaron 12.859 m2 de espacio público. </li>
            <li>Al interior del parque se construyeron 1.600 m2 de nuevos andenes y otros 5.289 fueron mejorados con condiciones de accesibilidad.</li>
            <li>Los seis puntos de acceso que mantendrán control de ingreso son: la calle 10 con las carreras 37, 38 y 40; la calle 8 con carrera 37 y 39; y la calle 9 con carrera 40.</li>
        </ul>

        <p>El Parque Lleras, referente comercial y turístico de Medellín, está nuevamente al servicio de los habitantes de la ciudad y de los visitantes que a diario llegan a disfrutar de los atractivos de la capital antioqueña. La Administración Distrital realizó en este sitio una intervención integral que permitió la renovación de 12.859 m2 de espacio público para dar prioridad a la movilidad peatonal, a la cultura, a las zonas verdes y a los espacios para compartir.</p>
        <p>Con la entrega, desde este martes se inició el “Abrazo al Lleras”, una iniciativa que gracias a lo eficiente que resultó en la Plaza Botero del centro de Medellín, se implementa ahora en este parque de El Poblado. A su alrededor se instalaron 100 vallas en los seis puntos de acceso que son regulados por uniformados de la Policía Metropolitana posibilitando el control de ingreso de drogas, armas y el efectivo control de ingreso de menores en horarios restringidos por el decreto de toque de queda y se espera.</p>
        <p>“Hoy nace un nuevo espacio peatonal para la ciudad, hoy recuperamos el Parque Lleras. Son kilómetros de espacio peatonal para los ciudadanos de Medellín y del mundo que van a encontrar un lugar maravilloso. Vamos a tener seguridad permanente con puntos de control que eliminan de tajo todas las modalidades delictivas que alguna vez se causaron acá. El mensaje es que las familias, los artistas vengan a visitar el Lleras, a los comerciantes que inviertan en el parque, que abran sus negocios”, dijo el alcalde Daniel Quintero Calle.</p>
        <p>Los seis puntos de acceso que mantendrán control son: la calle 10 con las carreras 37, 38 y 40; la calle 8 con carrera 37 y 39; y la calle 9 con carrera 40. El Parque Lleras cuenta con 25 cámaras inteligentes y 23 puntos de video vigilancia en total.</p>
        <p>Las zonas de Parque Lleras, Provenza, la 10 y Manila, conforman un cuadrante que ha sido intervenido desde inicios del año, con múltiples acciones de prevención, control y reacción por parte de la Policía Metropolitana y de manera articulada con la Alcaldía Distrital.</p>
        <p>Estas intervenciones han dado resultados positivos con la disminución de delitos, comparando lo que va de 2023 con el mismo periodo de 2022. El hurto a personas tiene una reducción del 31 % , el hurto a comercio una disminución del 38 % y en consecuencia el hurto a celulares presenta reducción del 52% y lesiones personales con reducción del 30 %.</p>
        <p>“Las grandes transformaciones en los territorios, cuando resultan acompañadas de toda la institucionalidad, al final lo único que redunda es en tener grandes éxitos y logros. Acá en este sector, vamos a crear esa red de apoyo de comerciantes de todo el sector, donde la seguridad no sea solo la policía. Vamos a cubrir cada uno de los puntos con equipo de policía judicial y de inteligencia”, dijo el Brigadier General, Carlos Humberto Rojas, comandante de la Policía Metropolitana.</p>
        <p>Entre la renovación física del parque se contemplaron, entre otras, la construcción de 1.600 metros cuadrados de nuevos andenes y el mejoramiento de otros 5.289 con condiciones de accesibilidad universal. Además de los trabajos propios del parque, también se mejoraron algunas vías aledañas y se ejecutó el cambio de redes húmedas y secas para adaptarlas a las necesidades actuales del sector.</p>
        <p>La inversión para los trabajos de construcción, mejoramiento y mantenimiento del espacio público en el Parque Lleras fue de $10.000 millones y los recursos fueron aportados por el Área Metropolitana del Valle de Aburrá. </p>
        <p>El nuevo Parque Lleras cuenta con un teatrino al aire libre para facilitar la oferta cultural en la zona. Además, en concordancia con la Ecociudad que promueve la Alcaldía de Medellín, fueron sembrados 98 nuevos árboles y 16.000 plantas menores. </p>
        <p>“Estas son obras que transforman el territorio metropolitano. Ha sido esta administración la que hizo una intervención real de cara al parque y a los ciudadanos. Son 6,8 kilómetros de andenes, 2.200 metros cuadrados de zonas verdes, y acá hay que destacar que no se taló ningún árbol, por el contrario, se van a sembrar nuevos que permiten conservar la fauna que está en este espacio”, destacó el director del Área Metropolitana, Juan David Palacio.</p>
        <p>Esta intervención beneficia a más de 134.000 personas de la comuna 14. Sin embargo, tiene especial impacto en por lo menos 100 comerciantes y 21 artesanos que trabajan alrededor del parque.</p>
        <p>“Nos parece muy bueno lo que está pasando con el parque porque es algo muy positivo, las mejoras se han visto mucho. Esperamos que todos los extranjeros y turistas se acerquen a conocerlo, quedó muy bonito. Los comerciantes estamos  muy agradecidos por esta renovación y todo lo que está pasando con el parque”, dijo Cristina Arango, comerciante del Parque Lleras.</p>
        <p>Las obras permitieron la generación de 70 empleos, 10 de ellos ocupados por mujeres. Durante las próximas semanas se realizarán trabajos adicionales en el sector para conformar un circuito peatonal que una al Parque Lleras con el Parque Principal de El Poblado.</p>
        <p>Desde esta semana, a través de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público-Privadas se realizará una intervención artística temporal sobre la carrera 38 y la carrera 40. Esta intervención será realizada por colectivos y artistas como Deuniti y Las Chicas que Pintan.</p>

        
        
