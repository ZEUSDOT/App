<h4 text-align="center">
Puentes verdes y jardines verticales impulsan la consolidación de Medellín como una ecociudad  
</h4>

								<hr>


								<ul>
                                    
                                <li>Se intervendrán cuatro puentes peatonales ubicados en los barrios Carlos E. Restrepo, Caribe, Conquistadores y Guayabal. 
</li>

<li> También se adecuarán espacios con muros verdes y jardines verticales en el Centro Administrativo La Alpujarra y en la fachada sur del Concejo de Medellín. </li>

<li>Hasta $1.124 millones se invertirán en la ejecución de las obras: 1.000 metros cuadrados de jardines verticales, 900 metros cuadrados de arte urbano y 200 metros cuadrados de muros verdes aproximadamente.  </li>


</ul>
                                <hr>

								<p>
                                    

                                Con el propósito de cualificar el paisaje urbano, la Alcaldía de Medellín adecuará cuatro puentes peatonales con jardines y obras de arte urbano, y mejorará los espacios de circulación internos y externos del Centro Administrativo La Alpujarra (CAM) con jardines verticales y muros verdes con sistemas inteligentes de riego. 
							
<p>


“Hacer de Medellín una Ecociudad es uno de los pilares fundamentales de nuestro Plan de Desarrollo Medellín Futuro. Llenarla de verde y de naturaleza es una misión de todos los miembros del conglomerado con la participación de la comunidad para beneficio de todos y del planeta”, puntualizó el director general de la Agencia APP, Rodrigo Foronda Morales.
<p>
Se trata de los puentes de la 33, entre el barrio Conquistadores y el Cerro Nutibara; en Colombia, entre Suramericana y el sector Carlos E. Restrepo; al sur, en la Avenida Guayabal, a la altura del Parque de Conservación Santa Fe; y al norte en la zona Caribe en la Secretaría de Movilidad. 
<p>
“Con esta iniciativa cualificamos el paisaje público, mejoramos la conectividad biológica y adecuamos espacios para enriquecer y consolidar el sistema ambiental de la ciudad con infraestructura verde alternativa”, indicó la secretaria de Infraestructura Física Natalia Urrego Arias.
<p>
También se aporta a la consolidación de estructuras verdes en la ciudad, se evita la demolición de los puentes con una reducción en la generación de huella de carbono y se avanza en la consolidación de una Ecociudad. 

<p>
Asimismo, para ofrecer una mejor atención a la ciudadanía y reducir la sensación de calor, en la fachada sur del Concejo de Medellín y en el Centro Administrativo La Alpujarra comenzará la instalación de jardines verticales, exactamente en las zonas de recepción (acceso y principal) y en el vacío central del primer piso del CAM. 
<p>
Esta intervención aporta a la producción de oxígeno, la reducción de partículas contaminantes y la generación de sensación de bienestar y luminosidad. Estas obras se suman al jardín vertical de 37 metros cuadrados entregado en pasados días en el primer piso del CAM, con 1.240 plantas sembradas. 
<p>

<p>
Este proyecto, que potenciará el fortalecimiento de una Ecociudad, el mejoramiento del paisaje urbano y una estadía cálida en el CAM a funcionarios, contratistas y ciudadanos, fue formulado y estructurado por la Agencia APP y será ejecutado en alianza con las secretarías de Infraestructura Física y Suministros y Servicios.  
</p>


