          <h2>Constelaciones, el mural artístico más grande de Medellín, se potencia como nuevo destino turístico y cultural </h2>
        </div>

        <ul>
            <li>Más de 500 casas fueron intervenidas con color para mejorar el paisaje urbano de los barrios Brisas del Jardín y San José de la Cima II, en Manrique. </li>
            <li>El macromural Constelaciones comprende una gran obra dirigida por el artista plástico Fredy Alzate y 30 murales más realizados por cuatro colectivos y dos artistas urbanos.</li>
            <li>Esta transformación urbana impulsa el turismo en el sector, y aporta a la seguridad y la convivencia ciudadana. </li>
        </ul>

        <section class="img-new">
            <a target="_blank"> 	<img src="{{ asset('assets/img/Banners/Constelaciones1.jpg')}}" class="img-fluid img-border"> </a>
        </section>
        <p>La Alcaldía de Medellín entregó a la ciudadanía el mural artístico más grande de la ciudad, Constelaciones, una obra de 14.819 metros cuadrados de nuevo arte urbano que comprende la intervención de más de 500 casas y 30 murales artísticos realizados por cuatro colectivos y dos artistas urbanos. </p>
        <p>Esta obra, realizada por la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas -Agencia APP-, bajo la conceptualización y coordinación del artista plástico Fredy Alzate, es el resultado de varias actividades como residencias artísticas para conocer en profundidad la zona, laboratorios creativos con los residentes, un reconocimiento activo del territorio, talleres de arte y un diagnóstico social que se ve plasmado en las diversas historias que cuentan los murales. </p>
        <p class="p-destacado">“Esto se construyó con los actores vivos del barrio, de la comuna. Los propios y los turistas van a ir recorriendo cada uno de esos murales y conociendo una historia de Medellín, del barrio, de los retos, de la resiliencia de una comunidad que ha sabido levantarse de los momentos más difíciles y lo ha hecho con arte, cultura y trabajo social. Todo el proyecto completo es una gran obra de arte. Eso va a significar un estallido turístico para la comuna 3. Desde ya les hago la invitación para que vengan a conocer esto tan maravilloso”. <br><br><span class="firmaNew">Expresó el alcalde de Medellín, Daniel Quintero Calle.</span> </p>
        <p>Constelaciones se complementa con un recorrido que comprende 30 murales realizados por los colectivos artísticos Jagua, Jauría, Biográfos y Lemm and Buda; y los artistas individuales Agüd y Dubián Monsalve, todos ellos con amplia experiencia en muralismo y arte urbano en la ciudad. En sus obras apostaron por retratar personajes, historias y momentos cotidianos de la vida del barrio. Entre ellas se destacan, Reminiscencias, Otras formas de cargar, Hogar con plantas, Trompos y canicas y Doña Rubiela. </p>
        <p>Esta obra es un conjunto de signos que parten de interpretar, expresar o evidenciar las relaciones en un territorio que se fue construyendo de manera colectiva por sus habitantes. Las diversas geometrías y esferas plantean un juego cromático con la “piel-ladrillo” de los barrios impactados. </p>
        <p>Fue conceptualizada y coordinada por Fredy Alzate, maestro en Artes Plásticas de la Universidad de Antioquia y magíster en Artes Plásticas y Visuales de la Universidad Nacional de Bogotá con exposiciones en el Museo de Antioquia, el Baluarte de Santa Catalina en Cartagena y el Banco de la República en Bogotá. </p>
        <p>“No queríamos imponer una imagen al barrio, sino que a partir de lo que ya estaba allí empezamos a hacer intervenciones muy puntuales y a darle una identidad a cada una de estas casas. En el juego de geometrías, y sobretodo de esferas, queríamos, metafóricamente, hablar de una constelación de un barrio y estas figuras donde aparece ladrillo expuesto también es atendiendo a que en la panorámica de Medellín es una textura muy presente y no queríamos ocultarla, sino que eso nos permitiera incluso aludir a las esferas y pensar una constelación de esferas y pensar que cada barrio es un mundo en la ciudad”, explicó el artista. </p>
        <p>Esta intervención también contó con distintas actividades en el territorio para promover su apropiación como Feria de Emprendimiento, jornadas de cine y 31 talleres de sensibilización artística con más de 1.126 participantes. Asimismo, se generaron 63 empleos, 29 de ellos en mano de obra; 24 de equipos artísticos y 10 logísticos.</p>
        <p>“No solamente es el cambio estético que le queremos dar al sector, es el proceso social y económico que pretendemos detonar. Son nuevos tours para que más personas de afuera del país vengan, así como nuestros turistas internos y habitantes de Medellín. Queremos que vean una nueva perspectiva de la ciudad, que conozcan sectores que no habían visitado nunca y que se dejen tocar de toda la magia de los murales. Esa es una forma de construir la Medellin Futuro”, indicó el director general de la Agencia APP, Rodrigo Foronda Morales. </p>
        <p>En la jornada de entrega, este sábado 6 de mayo de 2023, la comunidad se vinculó a través de juegos callejeros y participaron de la entrega de elementos alusivos a la gran obra que llena de arte el nororiente de Medellín que, junto con el Graffitour de la comuna 13, siguen consolidando a la ciudad como destino turístico nacional e internacional. </p>
        <p>Para Jhony Andrés Jaraba, habitante del barrio Brisas del Jardín, “Constelaciones está hecho a base de historias y de la naturaleza. En el macromural los diseños son espectaculares y consta de un recorrido muy espectacular y hermoso”. </p>
        <p>La Agencia APP, en su propósito de cualificar y mejorar el paisaje urbano, ha adelantado otras intervenciones de arte urbano que suman 30.263 metros cuadrados en espacios como el puente de la 4 Sur, la Galería Urbana a Cielo Abierto de la Calle Barranquilla, el intercambio vial Punto Cero, el Parque Lleras, entre otras, cumpliendo y superando la meta del Plan de Desarrollo de entregar 14.300 metros cuadrados de renovación urbana que fortalece la práctica de arte urbano, promueve la visibilidad de los grafiteros, artistas y muralistas, y transforma espacios para mejorar la convivencia y la seguridad.</p>

        
