						<p>En una apuesta por la transformación urbana, la cualificación del paisaje y el cuidado del planeta, Medellín le entrega a la ciudad el segundo muro verde más grande de Latinoamérica construido en edificación, ubicado en la fachada oriental del Centro Administrativo Distrital. Cuenta con 1.840 metros cuadrados y más de 31.000 canguros en los que se sembraron más de 96.000 plantas. </p>
						<p class="p-destacado">“Se está llenando la ciudad de muros verdes, y eso no solo es aire más limpio para la ciudad, construir parques en medio del centro, donde se creía que era difícil generar áreas verdes, muchas veces no se pueden hacer en áreas planas , pues se hacen en edificios, y realmente esto es hermoso. Pero además también tiene unos efectos sobre la temperatura del edificio y cuánta plata se gasta el edificio enfriando, desde la instalación del muro ha estado más bajita la temperatura a pesar de estos calores, esos muros atrapan parte del calor y hacen entre otras que haya que gastar menos energía”.<br>
						<br>
						<span class="firmaNew">afirmó el alcalde.</span></p>
						<p>Además de este imponente muro que mejora el paisaje de los funcionarios, contratistas y ciudadanos, también se intervino la fachada principal de la sede administrativa del Área Metropolitana del Valle de Aburrá. En esta obra se sembraron 35.516 plantas y se intervinieron 740 metros cuadrados. </p>
						<p>La construcción de estos muros verdes generó alrededor de 86 empleos, un poco más del 50 por ciento ocupado por mujeres como una apuesta por la equidad de género y el acceso a oportunidades laborales para esta población. La inversión total de este proyecto fue de $3.360 millones, aportados ciento por ciento por el Área Metropolitana del Valle de Aburrá.</p>
						<p class="p-destacado">“Son 1.840 metros cuadrados de muro verde, representados en 31 mil canguros y aproximadamente 96 mil plantas, que le dan un entorno urbanístico, paisajístico y ambiental muy distinto al centro de la ciudad. De la misma forma ya está lista la iluminación en la fachada de San Juan del Centro Administrativo Distrital. Son dos obras que estamos listos para entregar a la ciudadanía, que le dan un nuevo aire a la Comuna 10 y sobre todo al centro administrativo”.<br>
