						<p>La decisión se tomó luego de analizar las propuestas presentadas por los tres oferentes interesados en realizar la interventoría: Consorcio Metropol, Consorcio Intercárceles y Consorcio MZCJ 2024.</p>
						<p>En audiencia pública se dio a conocer que el ganador es el Consorcio Intercárceles; esto luego de analizar las propuestas. La decisión la tomó el comité evaluador.</p>
						<p>Las tres sociedades que integran este consorcio cuentan con trayectoria en interventoría de edificaciones públicas de gran magnitud, como el proyecto de construcción de la Ciudad de la Salud de Panamá, situada en ese país; establecimientos penitenciarios en Chile; y la interventoría para la construcción y adecuación de infraestructura educativa en Medellín.</p>
						<p>“Podemos estar ya tranquilos que un contratista de todas las calidades, que cursó un proceso de selección objetiva, va a estar mirando, va a estar inspeccionando la parte técnica, la parte financiera, jurídica y administrativa de ese gran proyecto que es la Cárcel Metropolitana”, dijo el subdirector de APP, Javier Rodas.</p>
						<p>A mediados de abril se firmará el contrato entre la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas -APP y el Consorcio Intercárceles por $13.315 millones para la interventoría, la cual inicia con la elaboración del cronograma de trabajo elaborado en conjunto con el concesionario Prodemex.</p>
						<p>Los diseños finales y la construcción de la Cárcel Metropolitana para Sindicados se desarrollarán en 36 meses, aproximadamente. El penal contará con un área de 30.000 metros cuadrados construidos, en la vereda Pedregal Alto, en el corregimiento San Cristóbal, que albergarán a 1.339 sindicados.</p>
						<p>El objeto de esta concesión, la primera de su género en el país de iniciativa público privada y con un plazo de 15 años, será construir, operar y mantener el centro penitenciario; así como adecuar la infraestructura y los servicios complementarios de alimentación, lavandería y dotación. El costo del proyecto asciende a los $675.013 millones.</p>
						<p>De esta manera, el Distrito se convierte en pionero en acatar los fallos de las Altas Cortes, sobre la obligatoriedad de las regiones metropolitanas de tener un centro de reclusión de estas características para cumplir con la ley vigente.</p>
