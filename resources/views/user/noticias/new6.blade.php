<h4 text-align="center">
La Alcaldía de Medellín decretó disminución de aportes por uso del espacio público para beneficio de comerciantes 
</h4>

								<hr>


								<ul>
                                    
                                <li>Esta medida favorece a comercios, bares y restaurantes, en el marco de la reactivación económica. 
</li>

<li>  En principio, la reducción de los aportes por uso y aprovechamiento económico del espacio público será de un 70 %.    </li>




</ul>
                                <hr>

								<p>
                                    

                                Como un apoyo a la reactivación económica y a la generación de empleo, la Alcaldía de Medellín estableció una reducción temporal en la retribución económica que hacen los establecimientos comerciales por ocupación y uso del espacio público, así como por la utilización temporal de vías, franjas de espacio público, parques, plazoletas, entre otros, para actividades afines a los servicios comerciales.
							
<p>


La disminución temporal, establecida en el Decreto 0967 de 2020, regirá hasta junio de 2021, y comprende una baja desde el 70 % en el primer mes hasta el 30 % en el noveno.  
<p>
Este apoyo económico es posible gracias a la articulación entre la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–, la Secretaría de Hacienda y la Subsecretaría de Espacio Público.
<p>
“La Administración Municipal, de la mano de los comerciantes, impulsa la reactivación económica de manera decidida. Prueba de esto es la firma del Decreto por medio del cual se reduce el cobro de la tarifa por uso y aprovechamiento del espacio público, inicialmente en un 70 %, luego en un 50 % y finalmente en un 30 %. Estos ahorros para los comerciantes pueden ser reinvertidos en rotación de inventarios, en generación de empleos y en el embellecimiento de sus establecimientos”, explicó el director general de la Agencia APP, Rodrigo Foronda Morales. 
<p>
Por ejemplo, un comerciante del sector de Provenza que normalmente realiza extensión de negocio para Aprovechamiento Económico del Espacio Público (AEEP) en cuatro metros cuadrados de espacio público, con los parámetros y requisitos establecidos en la norma, realizaría por los nueve meses unos aportes de $1.395.414, mientras que con la vigencia del Decreto pagaría $697.708 en el mismo periodo de tiempo, lo que representa ahorro de operación y la posibilidad de contar con mayor área para su labor. 


<p>
Esta medida también hará posible un aforo seguro y responsable que garantizará las medidas de bioseguridad como el distanciamiento social para mitigar la propagación del coronavirus. 
<p>
Asimismo, con la implementación de este incentivo económico se impulsará la activación de corredores y espacios estratégicos de la ciudad, el acompañamiento a la pequeña y mediana empresa, la prestación de los servicios comerciales con distanciamiento social y la ampliación y optimización de espacios disponibles y seguros para todos. 
<p>

<p>
Desde la implementación del instrumento de financiación del AEEP, que busca el mejoramiento del paisaje de Medellín, se han radicado más de 7.000 solicitudes ante la Administración Municipal, entre transitorias y temporales, con una retribución para la ciudad de más de $2.200 millones que se invierten en el mejoramiento del sistema público colectivo.  
</p>


