<h4 text-align="center">
Avanzan estudios para la modernización de la Unidad Deportiva Atanasio Girardot 
</h4>

								<hr>


								<ul>
                                    
                                <li>El sueño de renovar este espacio estratégico de ciudad alcanza un nuevo hito: el inicio de la etapa de factibilidad. 
</li>

<li>  Se contempla una nueva fachada y cubierta para el estadio con un área de 70.000 metros cuadrados y la construcción de una torre de parqueaderos.      </li>

<li>Los asesores del proyecto serán, entre otros, Francisco Maturana, Gilberto Giraldo Buitrago y la Sociedad Antioqueña de Ingenieros y Arquitectos.  </li>




</ul>
                                <hr>

								<p>
                                    

                               
Con el desarrollo de los estudios de factibilidad para la modernización de la Unidad Deportiva Atanasio Girardot, Medellín Futuro da un paso definitivo en el propósito de entregar a la ciudad un complejo moderno, idóneo para el encuentro de familias y amigos, donde se realicen eventos deportivos y culturales de talla internacional, con la gran oportunidad de generar más empleo y turismo.
							
<p>


Estos estudios, que se extenderán hasta septiembre de 2021, serán realizados por la Agencia para la Gestión, del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–.
<p>
“Este es uno de nuestro proyectos estratégicos; inicialmente dijimos que techo para el estadio, pero es más que eso, es un nuevo estadio que se va a adecuar, a construir, con todas las condiciones que hoy tienen los grandes estadios del mundo y que va a permitir no solo eventos deportivos, sino también espectáculos de gran nivel, de gran altura y de gran capacidad. Las ideas que tenemos en la cabeza son techo, conectividad de máximo nivel, parqueaderos, museo para los equipos, espacios comerciales", expresó el alcalde de Medellín, Daniel Quintero Calle. 
<p>
Comprenden el desarrollo de la planeación estratégica, los estudios iniciales y técnicos, los prediseños, la minuta del contrato APP y un informe final con todas las herramientas necesarias para la promoción del proyecto, la planeación y la toma de decisiones para las futuras etapas: aprobación, licitación y adjudicación de la iniciativa como alianza público privada. 
<p>
Para el director general de la Agencia APP, Rodrigo Foronda Morales, “la estructuración del estudio de factibilidad, que dará como resultado la alianza público privada que modernizará al estadio, la Agencia APP ha confeccionado el mejor esquema técnico de acuerdo con los lineamientos internacionales. Lo que buscamos es que tanto inversionistas nacionales como internacionales tengan certeza del cierre financiero que arrojará este estudio para entregar a Medellín el escenario que se merece”.


<p>
La renovación del estadio se proyecta en el marco de cumplimiento de la reglamentación de la Federación Internacional de Fútbol Asociación –FIFA–. Se intervendrán 163.700 metros cuadrados y se contempla, entre otras características, una cubierta con forma de elipse con un área de 70.000 metros cuadrados, parqueaderos soterrados con zonas VIP, restaurantes, mejoramiento de espacios de comidas, más rutas de evacuación, museo y fanzone, construcción de una sala de control, de un centro de atención médica y de un circuito cerrado de televisión, señalética, nuevos accesos a los espacios del cuerpo arbitral y de los equipos, y adecuaciones en silletería para brindar mayor confort e inclusión a personas con movilidad reducida. 
<p>
Por su parte, el edificio de parqueaderos se plantea como una nueva torre de 33.000 metros cuadrados, con seis pisos de 5.500 metros cuadrados cada uno, aproximadamente, que aportará a la renovación del paisaje urbano y que tendrá accesibilidad desde la calle 50. Se considera que tenga zonas comerciales, la Liga de Tenis de Mesa y un circuito perimetral de movilidad vehicular en cada nivel.
<p>

<p>
El estudio de factibilidad de estas obras estará liderado por un equipo de alto nivel conformado por Francisco “Pacho” Maturana, exfutbolista y director técnico de fútbol; Gilberto Giraldo Buitrago, economista industrial de la Universidad de Medellín, exgerente del Club Atlético Nacional y exasesor de Coldeportes Nacional; y Federico Mesa, arquitecto que ha participado en el diseño del orquideorama del Jardín Botánico (Lápiz de Acero), el Hotel Click Clack y el coliseo Iván de Bedout, entre otros, con la firma Plan B. 
</p>

<p>
El proceso también estará acompañado por Fabián Gonzalo Marín Cortés, magistrado auxiliar de la Sección Tercera del Consejo de Estado, durante 12 años, y subdirector de Gestión Contractual de Colombia Compra Eficiente; la Sociedad Antioqueña de Ingenieros y de Arquitectos; Óscar Manco López, PhD en Ciencias Económicas de la Universidad de París y en Ingeniería, Industria y Organizaciones de la Universidad Nacional de Colombia; y Orlando Bustamante Marín, ingeniero con treinta años de experiencia y ejercicio en gerencia integral de procesos de planeación estratégica.

</p>

<p>
“A través de los años la Unidad Deportiva Atanasio Girardot se ha convertido en el complejo deportivo más importante de la ciudad. Desde hace 67 años que se construyó el estadio y alrededor de él otros complejos que nos han convertido en uno de los referentes sudamericanos para eventos. Tener un estadio de talla internacional nos permitirá tener los mejores y más grandes certámenes deportivos y del espectáculo que contribuyen, además, a la reactivación económica, al desarrollo del turismo y a seguir viendo a Medellín como la capital del deporte”, afirmó la directora del INDER, Diana Paola Toro Zuleta. 
</p>

<p>
Para esta etapa de factibilidad se tendrá un diálogo incluyente, participativo y articulado con las personas que se beneficiarán del proyecto como comerciantes, organizaciones sociales, empresas y ciudadanos, entre otros, para conocer su expectativas, necesidades, sueños y temores.
</p>

<p>
La modernización de la Unidad Deportiva Estadio Atanasio Girardot permitirá mejores condiciones técnicas para eventos regionales y mundiales, fortalecerá la oferta comercial, ampliará la capacidad de espectadores y de parqueaderos, conectará los diferentes escenarios con más y mejores espacios públicos y aportará a consolidar una Ecociudad con un distrito deportivo renovado, amigable con el medio ambiente y condicionado para el disfrute. 
</p>


