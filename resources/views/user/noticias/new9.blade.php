<h4 text-align="center">
Artistas y colectivos llenarán de arte urbano la calle Barranquilla
</h4>

								<hr>


								<ul>
                                    
                                <li>27 propuestas fueron preseleccionadas para la nueva galería urbana a cielo abierto de Medellín, en el barrio Prado.
</li>

<li> Serán aproximadamente 3.200 metros de intervención entre las estaciones Hospital de Metroplús y Palos Verdes.      </li>

<li>Esta obra, que fortalece el arte urbano, hace visible la importancia del graffiti y de las manifestaciones culturales en la ciudad.  </li>




</ul>
                                <hr>

								<p>
                                    

                                Artistas, muralistas, grafiteros y colectivos fueron invitados por la Alcaldía de Medellín a través de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP– a renovar con imaginación y color la calle Barranquilla, que conecta al centro con el nororiente, y por donde han dejado huella reconocidas manifestaciones del graffiti.
							
<p>


En total se presentaron 82 portafolios y fueron preseleccionados 27 que representan el contexto de la calle Barranquilla, la pertinencia social y cultural, así como los conceptos de memoria, patrimonio, ciudad de sectores cercanos como Prado, Manrique, Lovaina y Sevilla.
<p>
Se valoró la trayectoria en el arte urbano gráfico, el dominio de las técnicas del graffiti o del muralismo en gran formato, la capacidad de relacionar aspectos conceptuales en la obra, el compromiso y la coherencia con los contextos sociales e históricos. 
<p>
La curaduría estuvo a cargo de Juan David Quintero, historiador y gestor cultural con una trayectoria de 15 años en investigación, museografía y desarrollo de proyectos; y  Alejandro Villada, conocido como “Pac Dunga”, artista del grafiti, miembro del movimiento del hip hop desde la década de los ochenta y con un trabajo orientado a la simbología étnica, afrodescendiente y diversa del país. 
<p>
Serán aproximadamente 3.200 metros de arte urbano entre las estaciones Hospital de Metroplús y bajos de la carrera 48, muy cerca de Palos Verdes, en el barrio Manrique. 


<p>
“Los muros tendrán una intervención especial porque se tiene en cuenta el contexto del sector y la importancia de cada grafiti o mural existentes. Cada muro tiene una radiografía o ficha en la que se explica su estado, la obra que contenía y su potencial artístico”, explicó el director general de la Agencia para la Gestión del Paisaje, el Patrimonio y las Alianzas Público Privadas –Agencia APP–, Rodrigo Foronda Morales. 
<p>
Esta es una de las acciones contempladas en el Plan de Desarrollo Medellín Futuro 2020 – 2023, liderada por la Agencia APP, con el propósito de cualificar el paisaje y generar más espacios para el disfrute y uso de las personas. Asimismo es una iniciativa que fortalece la memoria del grafiti y de los movimientos musicales y culturales de la ciudad.
<p>

