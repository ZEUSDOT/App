        </div>             
              <p>
              El patrimonio inmueble de Medellín está compuesto por una serie de bienes que están clasificados en listados de Bienes de Interés Cultural (BIC) y la Lista Indicativa de Candidatos a Bienes de Interés Cultural (LICBIC). Los Bienes de Interés Cultural (BIC) son aquellos bienes inmuebles a los que se les atribuye un especial interés histórico, estético o simbólico en diversos ámbitos, se puede tratar un bien en particular o un conjunto de bienes los cuales deben ser conservados como una unidad indivisible. La Lista Indicativa de Candidatos a Bienes de Interés Cultural (LICBIC) es un registro de información de los bienes inmuebles susceptibles de ser BIC, no implica ningún tipo de protección, sin embargo sus características responden a criterios de valoración patrimonial de importancia para el territorio.
