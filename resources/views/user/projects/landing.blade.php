     <p>Está enfocado en la gestión que complementa el Plan Especial de Manejo y Protección Patrimonial (PEMP) para la puesta en valor del patrimonio cultural, arquitectónico, urbano y paisajístico del barrio Prado, mediante la cualificación del espacio público y el patrimonio arquitectónico, la participación comunitaria y la inserción de nuevas dinámicas económicas relacionadas con el arte, la cultura y el turismo.</p>
