        </div>

        <div class="row content">
          <p>El patrimonio cultural de la nación es el conjunto de todos los bienes materiales, las manifestaciones inmateriales, los productos y las representaciones de la cultura que son expresión de la nacionalidad colombiana, tales como la lengua castellana, las lenguas y dialectos de las comunidades indígenas, negras y creoles, la tradición, el conocimiento ancestral, el paisaje cultural, las costumbres y los hábitos, así como los bienes materiales de naturaleza mueble e  inmueble a los que se les atribuye, entre otros, especial interés histórico, artístico, científico,  estético o simbólico en ámbitos como el plástico, arquitectónico, urbano, arqueológico, lingüístico, sonoro, musical, audiovisual, fílmico, testimonial, documental, literario, bibliográfico, museológico o antropológico.  (Ley 1185 de 2008, artículo 1°, que modifica el artículo 4° de la Ley 397 de 1997).</p>
