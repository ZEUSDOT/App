            <p> Con esta iniciativa se busca revitalizar el patrimonio urbano más importante de la ciudad; contar con un escenario cultural único; generar mayor presencia institucional; fortalecer la confianza ciudadana; promover la reactivación económica del barrio; contribuir a la conservación de los bienes inmuebles y mejorar la seguridad del sector.</p>
