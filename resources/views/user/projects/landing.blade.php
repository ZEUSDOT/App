          <h2>Gestión del Patrimonio </h2>
        </div>

        <div class="row content">
          <p>El cuidado y conservación del patrimonio cultural nos compete a TODOS, y así ha quedado consignado en la Ley General de Cultura, al establecer que: “Es obligación del Estado y de las personas valorar, proteger y difundir el Patrimonio Cultural de la Nación.” (Artículo  numeral 5, Ley 397 de 1997)</p>
          <p>Como su contenido es tan extenso se privilegian algunos bienes y manifestaciones, conforme a criterios de valoración y requisitos reglamentados por el Ministerio de Cultura, a los que se les define un régimen especial de salvaguardia, protección, sostenibilidad, divulgación y estímulo; a esto se le denomina PATRIMONIALIZACIÓN. Los de naturaleza material son declarados como Bienes de Interés Cultural (BIC), y los de naturaleza inmaterial se incluyen en la Lista Representativa de Manifestaciones del Patrimonio Cultural Inmaterial (PCI). </p>
        </div>
